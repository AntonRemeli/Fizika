A világot megtapasztalva a Mindenséget a következő összetevőkre bonthatjuk:
<br><br>
(<b> m </b>)<b> TÖMEG</b>: A nyugvó rendszer (mely állhat akár csak egy objektumból vagy objektumok tömkelegéből) megtartja nyugalmi állapotát mindaddig, amíg egy külső hatás annak megváltoztatására nem kényszeríti... ezen tehetetlenség mértéke a tömeg (<b> m </b>, idegen szóval: massa), mely megmutatja, mennyire nehéz valamit megmozdítani helyzetéből
<br><br>
(<b> I </b>)<b> LENDÜLET</b>: A mozgásmennnyiség (<b>I = m * v </b>, idegen szóval: impulzus, momentum vagy "linear momentum" = "egyenesvonalú lendület") megmutatja, hogy egy adott dolog mennyire akarja megtartani a mozgását, vagy ütközéskor mekkora lendületet adhat át egy másik testnek
<br><br>
(<b> F </b>)<b> ERŐHATÁS</b>: Erőhatás okozza a kölcsönható testek lendületváltozását: <b> F = ΔI/Δt </b>("Δ" a változás jele, továbbfejtve, <b>I = m * v </b> behelyettesítésével:) <b>= m * Δv/Δt = m * a Newton II. törvényét kapjuk</b>,
<br><br>
</i>
Azonban az egyenesvonalú (lineáris) mozgáson túl  a világra nagyon is jellemző a <b>körmozgás</b>, <br>
<i>( ugyanis, azon túl, hogy valaminek lehet egyenesvonalú lendülete (idegen szóval: lineáris momentuma vagy lineáris impulzusa), a testeknek (rendszereknek) lehet igen erős forgása is mely, ugyanúgy mint a lendület önmagától nem szűnik meg)</i><br>

így a  lineáris tömeg (<b> m </b>), lineáris lendület (<b> I </b>) és lineáris erőhatáson (<b> F </b>) túl a "körtömeg", "körlendület" és "körerő" fogalmát is be kell vezetni ahhoz, hogy a búgócsiga őrült pörgésével elmesélhesse a világ történetét:
<i>
<br><br>
(<b> n </b>)<b> KÖRTÖMEG</b>: A nyugvó rendszer (mely állhat akár csak egy objektumból vagy objektumok tömkelegéből) megtartja nyugalmi állapotát mindaddig, amíg egy külső hatás annak megváltoztatására nem kényszeríti... ezen tehetetlenség mértéke a <b> körtömeg </b>(<b> n = m * k </b>, idegen szóval:  "moment of inertia" = "tehetetlenségi nyomaték",  vagy "rotational inertia" = "forgási tehetetlenség", avagy "angular mass" = "szögtömeg"), mely megmutatja, mennyire nehéz valamit "kifordítani" helyzetéből (<b> k </b> a körpálya középpontjától, a forgási tengelytől mért távolság, az erőkar hossza).
<br><br>
(<b> N </b>)<b> KÖRLENDÜLET</b>: A <b>körmozgásmennnyiség</b> ( <b>N = I * k </b>, jó magyar elnevezései "perdület", "impulzusnyomaték" avagy "lendületnyomaték", idegen szóval: "angular momentum" = "szöglendület" és "rotational momentum" = "forgási lendület") megmutatja, hogy egy adott dolog mennyire akarja megtartani a körforgását, vagy kölcsönhatáskor mekkora körlendületet adhat át egy másik (helyhezkötött de körmozgásra képes) testnek
<br><br>
(<b> M </b>)<b> KÖRERŐHATÁS</b>: Az erő forgatónyomatéka okozza a kölcsönható testek körlendületváltozását: <b> M = ΔN/Δt </b>("Δ" a változás jele, továbbfejtve, <b>N = I * k </b> behelyettesítésével az erőkar törvényét kapjuk:) <b>= ΔI/Δt * k = F * k</b> ( idegen szóval: "torque" = "forgatónyomaték", "momentum" = "nyomaték", vagy "moment of force" = "erőnyomaték" )
<br><br>

<br>
És itt jön most képbe a búgócsiga meséje:
<br><br>
Bármennyire is viszonylagos az egyenesvonalú egyenletes mozgás (megfelelő koordinátarendszerből nézve bármely mozgás nyugvásnak értelmezhető), annyira egyértelműen abszolút a forgómozgás:
<br><br>
- egyenesvonalú egyenletes mozgás esetén mondhatjuk, hogy valóban mi állunk, és a világ suhan el mellettünk
<br><br>
- pörgés esetén azonban nem lehet mondani, hogy nem forgunk, és az egész világ forog körülöttünk ("forog körülöttem a világ" mondás azonban létezik), 
<br><br>
...ugyanis gondolatkísérletnek képzeljünk el egy ultrakönnyű de szupererős körhintát (tehát mi sokkal "nehezebbek" vagyunk a körhintától):
<br>
- a nyugvó körhintán semmi különöset nem érzünk, mozoghatunk kifelé vagy befelé közelítve a körhinta forgástengelyéhez,
<br>
- forgó körhintán azonban <b> F<sub>cf</sub> = m * v<sup>2</sup> / k </b>centrifugális erő "húz" minket kifelé a hintából (szerencsére ugynolyan nagyságú centripetális erő húz minket befelé a szék támlájának nyomása által). 
<br>
- a belső székbe átülve (félúton a forgástengelyhez) forgásunk jelentősen gyorsabb lesz (körlendület megmaradása:  <b>m*v * k = m*2v * k/2</b>), a kerületi sebességünk kétszer lesz nagyobb, de mivel kétszer rövidebb köröket kell befutnunk, a fordulatszám négyszer (!) lesz nagyobb, a centrifugális erő pedig <b>F<sub>cf</sub> = m * (2v)<sup>2</sup> / k/2 </b> nyolcszor (!) lesz nagyobb.
