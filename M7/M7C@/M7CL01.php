Minél forróbb valami, annál jobban sugároz, így az alakulóban lévő <b>protocsillag</b> először vörösen kezd izzani, majd sárgán, majd fehéren, majd szúró kék színben. A heves ütközésekben a hidrogénatomok egymásról letépik a kísérő elektront, hidrogénplazma jön létre, meztelen protonok száguldanak, melyek egy pillanatban foglyul ejtenek egy arra kószáló elektront (mivel a pozitív töltésük nagyon erősen vonzza a leszakított negatív elektronokat), az elektronbefogást fénykibocsájtás követi. De ez a fény még mindig igen halovány, ez még nem igazi csillagfény.
<br>
A születő csillag közepében a nyomás és hőmérséklet olyan nagy lesz, hogy az egymásnak rontó hidrogénatomok, jobban mondva: hidrogéniónok, azaz protonok a kölcsönös elektromos taszítás ellenére mégis olyan közel kerülnek egymáshoz, hogy felülkerekedik az erős kölcsönhatás (még akkor is, ha nem hágják meg a potenciálgátat, de az úgynevezett <b>kvantumtunneleffektus</b>nak köszönhetően mégis erős kölcsönhatásba lépnek), és deutériumatommagot alkotnak, két deutérium pedig héliumot.

