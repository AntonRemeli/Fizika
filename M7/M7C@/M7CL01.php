A haláltusáját vívó, mind gyorsabban forgó zsugorodó csillag közepében a nyomás és hőmérséklet olyan nagy lesz, hogy az egymásnak rontó héliumatomok, jobban mondva: héliumiónok (kettő proton  + kettő neutron)  a kölcsönös elektromos taszítás ellenére mégis olyan közel kerülnek egymáshoz, hogy felülkerekedik az erős kölcsönhatás (még akkor is, ha nem hágják meg a potenciálgátat, de az úgynevezett kvantumtunneleffektusnak köszönhetően mégis erős kölcsönhatásba lépnek), és a hélium berilium, szén és oxigénné alakul át (kettő, három és négy héliumatom "összeolvadásával"). A héliumégető Nap (tízszer kevesebb energia szabadul fel mint hidrogénégetésnél) vörös óriássá puffad: egymillió kilóméteres sugara  kétszázmillió kilóméteres lesz, a Földet is magába olvasztja (forgása jelentősen lelassul, egy hónap helyett ezer év alatt fordul meg tengelye körül).
