Hideg felületeken lecsapódik a levegő páratartalma, megjelenik a
penészgomba. A megemelt hőmérsékletű falfelület kevésbbé hajlamos
páralecsapódásra, és ebből kifolyólag kevésbbé hajlamos penészesedésre. 
Az imént elhangzott gondolat egy állítás. Felvetődik a kérdés: 
engedelmeskedik-e a valóság a gondolataink által szabott szabályoknak?
<br>
<br>
Nem feltétlenül! És különösen akkor nem, ha bizonyos tényezőket kifelejtünk. 
Ezért megpróbálunk kellő körültekintéssel gondolkozni. És még akkor sem 
lehetünk biztosak abban, hogy mindent figyelembe vettünk. 
Perdöntő bíró csak egy van: a KÍSÉRLET.
<br><br>
Azonban, ha csak kísérletekre építenénk boldogulásunkat a világban,
akkor végtelenül sok kísérletre volna szükségünk. Tehát (szegény embernek) marad mégis az elmélkedés (ha nem túl lusta hozzá):
<br><br>
Amennyiben olyan teremben vagyunk, melyben némely testek magasabb T+dT
hőmérsékletűek a környező T hőmérsékletű felületekhez képest, akkor
alapfelállásban, normális bevonatok esetében a hőtükrözés (és annak
reciproka a hőelnyelés=hősugárzás=1-hőtükrözés, rövidebben: a=e=1-r)
azonos a környező falak
T=290K (17C fok) és a hősugárzó felület T+dT=310K (37C fok)
hőmérsékletén.<br>
<br>
Azok a speciális festékek, melyek elnyelőképessége nagyobb a T+dT
hőmérsékleten mint sugárzóképességük T hőmérsékleten, kénytelenek
lesznek T hőmérsékletüket némileg megemelni&nbsp; T+ddT -re ahhoz, hogy
leadhassák az általuk elnyelt többletenergiát ( az áramlásos és
vezetéses hőveszteségek első közelítésben gyakorlatilag azonosak maradnak a T és T+ddT
hőmérsékletű felületek mentén), az így megnövekedett hősugárzás T+ddT hőmérséklet
negyedik hatványával lesz arányos, ami kicsi ddT esetében
(T+ddT)<sup>4</sup>=T<sup>4</sup>*(1+4*ddT/T)
lesz ( ugyanis ddT=3C csak 1% a T=290K-hez viszonyítva).<br>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
Normál&nbsp; falfelület : 17C fokon 400W/m<sup>2</sup>
hőt sugároz.&nbsp; Egy hagyományos építésű ház lyukacsos téglával rakott
fala 40W/m<sup>2</sup> hőt vezet el a -3C hideg kültérbe.&nbsp; A szoba
fala 400+40W/m<sup>2</sup>
hőenergiát vesz fel, 400-at visszasugároz (17C fokon kialakul egy belső zárt sugárzási tér, a falfelület minőségétől függetlenül feketetest sugárzási jellemzővel), a hősugárzótól kapott 40W/m<sup>2</sup>
energiát elvezeti a kültérbe (a fűtőtest úgy van beállítva, hogy pont kompenzálja a falak hőveszteségét).&nbsp;&nbsp; <br>
<br>


Penészgátló csodafesték alkalmazásával a belső falfelület 
400+(40+4%)W/m<sup>2</sup> hőenergiát vesz fel, 17C-on 400W/m<sup>2</sup>
hőenergiát visszasugároz, a hősugárzótól kapott 40+4%=41.6W/m<sup>2</sup> energiából 40W/m<sup>2</sup>hőt elvezet a kültérbe, 1.6W/m<sup>2</sup> pedig 0.4%-kal megnöveli a visszasugárzást a szobába (0.4%=1.6/400), ami 0.4%=4ddT/T alapján ddT=0.3C (=0.4%*T/4=0,1%*290=0.29=0.3) fokkal megemeli a csodafesték felszíni hőmérsékletét. <br><br>

Ha a belső falfelület 17C helyett most 17.3C, akkor a hővezetés 0.3/20=1.5%-kal
megnövekszik 41W/m<sup>2</sup>-re. Mivel második közelítésben&nbsp; a
hősugárzótól kapott 41W/m<sup>2</sup>
energiából 41W/m<sup>2</sup> elmegy a kültérbe, a visszamaradó 1W/m<sup>2</sup>&nbsp;
0.25%-kal megnöveli
a visszasugárzást a szobába, ami 0.25%=4ddT/T alapján ddT=0.2C fokkal
emeli a fal felszíni hőmérsékletét. Így a hővezetés csak
0.2/20=1%-kal, azaz 0.5W/m<sup>2</sup>-rel lesz
nagyobb, a visszasugárzásra maradó  1.5W/m<sup>2</sup>
0.37%-kal
lesz nagyobb a 400W/m<sup>2</sup>-hez képest, a fal
hőmérséklete 0.3C fokkal magasabb. A közelítő
számítások végén a csodafestékkel bevont fal hőmérséklete
megközelítőleg 0.25C fokkal lesz magasabb...<br>
<br>
Ez az elenyésző enyhén megemelt hőmérsékletű falfelület nem lesz
kevésbbé hajlamos páralecsapódásra, és ebből kifolyólag nem lesz
kevésbbé&nbsp; hajlamos penészesedésre. A nyereség elenyésző...<br>
<br>
Külső hőszigetelés megduplázása útján (5cm hungarocell borítással) a falon át elvezetett hő
20W/m<sup>2</sup>-re csökken. A belső fal melegebb lesz, befelé 4%-kal
nagyobb
400+16W/m<sup>2</sup>&nbsp; sgárzás 3C fokkal melegebb falat jelent, a
3C fokkal nagyobb gradiens
3/20=15%-kal, 20W/m<sup>2</sup>-ről&nbsp; 23W/m<sup>2</sup>-re
megnöveli a hővezetést kifelé.
Az igazi nyereség nem a melegebb belső falfelület lesz, hanem a
hőveszteségek majdnem megfeleződése.<br>
<br>
Amennyiben azonban gőzfürdőt üzemeltetünk, akkor a 20C meleg falon is
le fog csapódni a levegő kimagaslóan magas páratartalma és beindul a
fal penészgombásodása. Egyedüli mentség a penészgombától a levegő
páratartalmának kellő mértékű lecsökkentése úgy, hogy a falakon
páralecsapódás ne forduljon elő...<br>
<br>
... és a legfontosabb dolog, a hőszigetelés vastagságától, csodafesték
minőségétől független: nem szabad hőhidakat hagyni, a többi
falfelülettől sokkal hidegebb felületek kialakulását meg kell
akadályozni. A szobalevegő párája a hideg részeken fog lecsapódni,
megfelelő táptalajt biztosítva a penészgombásodásnak...<br>
<br>
... és egy jó tipp: ha mégsem tudjuk a hőhidakat megszűntetni, akkor a hidegebb részeket dörzsöljük be viasszal (vagy fessük át víztaszító vízhatlan festékkel), hogy a felület víztaszító legyen, ne szívja meg magát nedvességgel...


