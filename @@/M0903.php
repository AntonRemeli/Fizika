<br><br>
A 3-4μm tartományban a fehér bevonatok reflektivitása átmenetileg 5%-ról 30%-ra emelkedik, majd a 6-6.5μm tartományban 4%-ról átmenetileg 12%-ra emelkedik (3M által publikált spektrális emissziós grafikonokból) . Ez lehet a magyarázata a melegebb, kondenzáció- és penészgombamentes falaknak, ám ehhez nem kell feltétlenül valamilyen csodafesték: a mindennapi CaC3<sub>3</sub> mészfesték is megteszi.

