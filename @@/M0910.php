A hőszigetelő nanokerámiás bevonatok hővezetési tényezőjét nem mérték meg. Amikor pedig megmérték, a kapott érték legjobb esetben a nikecell 0.04W/mK értékét érte el (roszabbik esetben az akrilfesték 0.07W/mK értékét kapták), a reálisnak becsült, aerogéllel veteksző 0.01W/mK nem lett megmérve, még kevésbbé a 0.001W/mK csodaérték (mely akkor az állított és csak orosz intézmény által megmért 2W/m<sup>2</sup>K kültéri hőátadást eredményezné).<br>
Mivel a hővezetési tényező az anyag sűrűségével arányos, a nanofesték sűrűsége pedig az akrilfestéktől alacsonyabb (mert üreges kerámiagömböket kevernek az akrilfestékbe), de nem érheti el az aerogélek ultraritka szerkezetét, úgy legjobb esetben is hővezetési tényezője a nikecellal vetekedhet  (miközben a nikecelltől jelentősen magasabb mechanikai szilárdságot kell tanúsítania). Ezért megújított eszmefuttatással a következőt állíthatjuk nagy bizonyossággal:
<br><br>
1mm nanokerámiás hő-védő bevonattal  100C fok hőmérsékletkülönbség esetén a hővezetés: <br>
  - 4kW/m<sup>2</sup>-re korlátozódik (amennyiben hővezetési tényezője 0.04W/mK, ami a nikecell tulajdonsága),  tehát a hő-védő bevonat hőgátat ugyan nem képez, mert hővezetése a hősugárzás és hőátadás 2kW/m<sup>2</sup> becsült értékével azonos szinten van, de ez is elégséges ahhoz, hogy 130C helyett a cső külső, most már befestett felülete 97C legyen, ezzel a hővezetést  két harmadára csökkentve (1/4+1/2=3/4=1/1.33) mégis egy harmadával csökken az ipari csővezeték hővesztesége!<br><br>
  - 2kW/m<sup>2</sup>-re korlátozódik kétszeres bevonással, a hő-védő bevonat hőgátat továbbra sem képez, mert hővezetése a hősugárzás és hőátadás 2kW/m<sup>2</sup> becsült értékével azonos szinten van, de ez is elégséges ahhoz, hogy 130C helyett a cső külső, most már befestett felülete 80C legyen, ezzel a hővezetést  felére csökkentve (1/2+1/2=1=1/1) felére csökken az ipari csővezeték hővesztesége!<br><br>
  
  Természetesen, 10mm nikecell vagy kőzetgyapot alkalmazása a hővezetést 0.4kW/m<sup>2</sup>-re korlátozná, ami már valódi hőgát, tehát a hő-védő burkolás hőgátat képezve ötödére csökkenti az ipari csővezeték hőveszteségét.
