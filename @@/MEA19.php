Tíz liter ( <b> m = 10 kg </b> ), jéghideg viz felmelegítése forrásig ( <b> ΔT = 100C = 100K fok</b> ), egy óra és tíz percet vesz igénybe
<br>
( <b> t = 1h 10p = 70p = 3600s+600s = 4200s</b> ) az 1kW teljesítményű (<b> P = 1 kW </b>)  főzőlapon. 
<br><br>
Az elfogyasztott elektromos energiát a teljesítmény és idő szorzata adja:
<br><br>
<b> W = P * t = 1 kW * (1+1/6) h = (1+1/6) kWh </b>  aminek költsége 50 Ft körül van. Ugyanez SI mértékegységekben:
<br><br>
<b> W = P * t = 1 kW * (1+1/6) h = 1000 W * 4200 s = 4 200 000 Ws = 4200 kJ  = 4.2 MJ</b> 
<br><br>
Egy liter víz egy fokkal való felmelegítéséhez ezerszer kevesebb energia kell,
<br>
tehát a víz fajhője  <b> c<sub>víz</sub> = W /m /ΔT = 4,2 kJ/kgC </b> 
<br><br>
A vas fajhője tízszer kisebb,  <b> c<sub>vas</sub> = W /m /ΔT = 0.4 kJ/kgC </b> , 
<br>
tehát a 10kg tömegű vasöntvény a főzőlapon már  csupán hét perc alatt száz fokra melegszik !
<br><br>
A föld, kőszikla, beton fajhője négy-ötször kisebb mint a vízé, tehát <b> c<sub>f,k,b</sub> = 1 kJ/kgC </b> .
<br><br>
Rövidesen bebizonyosodunk, hogy a levegő fajhője is <b> c<sub>lev</sub> = 1 kJ/kgC </b> .
<br><br><br><br>

Figyelembe véve hogy:
<br><br>
<b> p = 1 Atm = 100 000 N/m<sup>2</sup></b> atmoszferikus nyomáson
<br>
<b> T = 0 C = 273 K</b> fok hőmérsékleten
<br>
<b> V = 22 000 l = 22 m<sup>3</sup></b> térfogatot foglal el egy kilomól mennyiségű gáz  ( ezer Avogadro számú gázrészecske, <b> N<sub>Ae</sub> =  6*10<sup>26</sup> </b>) 
<br>
fajlagos belső energiája:
<br><br>
<b>  R<sub>e</sub>  = p * V / T = 100 000 N/m<sup>2</sup> * 22 m<sup>3</sup> / 273 K =  2 200 000 Nm /273 K = 8.3 kJ/K </b> 
<br>
( ahol <b>R<sub>e</sub> </b> a Regnault féle gázállandó egy kilomól mennyiségű, ezer Avogadro számú gázrészecskére).
<br><br><br><br>

Ismert, hogy a levegő összetétele 80% nitrogén (2 *14 kg moláris súly), 19% oxigén (2 *16 kg moláris súly) és 1% széndioxid ( 12 + 2 *16 kg moláris súly), tehát <b> m = 29 kg </b> körül van egy kilomol levegő tömege.  
<br><br>
- Mivel nulla fokon  <b> V = 22 m<sup>3</sup></b> térfogatot tölt ki az <b> m = 29kg </b> tömegű egy kilomol levegő, egyből látjuk, hogy a levegő fajsúlya  <b> ró = 1.3 kg / m<sup>3</sup></b> .
<br><br>
- Továbbá mivel  egy 22 m<sup>3</sup>-es zárt tartályban melegített kilomol levegő  <b> ΔT = 1C </b> fokkal való felmelegítéséhez 
<br>
<b> ΔE<sub>b</sub>  =  ΔE<sub>k</sub>  = f/2 n<sub>e</sub> * R<sub>e</sub> * ΔT  = 5/2 * 8.3 kJ/K * 1C = 21 kJ/C * 1C  = 21 kJ</b>  hőenergia szükséges, 
<br>
úgy a zárt edényben tartott  <b> m = 1 kg </b> levegő fajhője <b> c<sub>v,lev</sub> = ΔE<sub>b</sub> / ΔT / m = 21 kJ/C / 29 kg = 0.7 kJ/kgC</b>.

<br><br>
- Mivel azonban a Föld légköre nincs bezárva egy zárt tartályba, hanem inkább a levegőoszlop saját sulyából eredő 
<br>
<b> p = 1 Atm </b> nyomás az állandó,  a felmelegített levegő pedig kitágul, 
<br>
úgy a melegítés egyik fele a részecskék mozgási sebességének ( a hőmérséklet) növelésére használódik el: 
<br>
<b> ΔE<sub>b</sub>  =  ΔE<sub>k</sub>  = f/2 n<sub>e</sub> * R<sub>e</sub> * ΔT  = 5/2 * 8.3 kJ/K * ΔT = 21 kJ/C * 1C  = 21 kJ</b> , 
<br>
másik része pedig a térfogat növelésére (szét kell nyomnia az őt szorongató környező levegőt, ami nem kis munka!): 
<br>
<b> p * ΔV =  n<sub>e</sub> * R<sub>e</sub> * ΔT  =  8.3 kJ/C * 1C = 8.3 kJ</b> . 
<br>
Így mindösszesen <b> Q = 29 kJ </b> 	hőenergia szükséges <b> m = 29 kg </b> levegő <b> ΔT  =  1C </b>  fokkal való felmelegítéséhez,
<br>
tehát a levegő fajhője a Föld felszínén, állandó nyomáson  valóban <b> c<sub>lev</sub> = 1 kJ/kgC </b> : 
<br>
<b> c<sub>p,lev</sub> = Q / ΔT / m = ( ΔE<sub>b</sub> + W<sub>szétnyom</sub> ) / ΔT / m = ( 21 + 8 ) kJ/C / 29 kg = 1 kJ/kgC</b>.
<br><br><br><br>

A fenti számítás a hidrogéngázra is ugyanúgy érvényes, azonban mivel egy kilomol     hidrogéngáz súlya csupán <b> m = 2 kg </b> , 
<br>
úgy a hidrogéngáz fajhője 14-szer nagyobb mint a levegőé: <b> c<sub>p,H2</sub> = 14 kJ/kgC </b> !
<br><br><br>

Vegyük azonban észre, hogy moláris egységben mérve az egy kilomol  <b> V = 22 m<sup>3</sup></b>   és <b> m = 29 kg </b>  tömegű levegő hőkapacitása
<br>
<b> C<sub>p,lev</sub> = Q / ΔT = ( ΔE<sub>b</sub> + W<sub>szétnyom</sub> ) / ΔT  = ( 21 + 8 ) kJ/molC = 29 kJ/molC</b>
<br>
megegyezik az egy kilomol  <b> V = 22 m<sup>3</sup></b>   és <b> m = 2 kg </b>  tömegű hidrogéngáz hőkapacitásával:  
<br>
<b> C<sub>p,H2</sub> = Q / ΔT = ( ΔE<sub>b</sub> + W<sub>szétnyom</sub> ) / ΔT  = ( 21 + 8 ) kJ/molC = 29 kJ/molC</b>.
<br><br><br>

Továbbá azt is vegyük észre, hogy moláris egységben mérve csupán három hőkapacitás létezik:
<br>
-  <b> C<sub>1</sub> = Q / ΔT = ( ΔE<sub>b3/2</sub> + W<sub>szétnyom</sub> ) / ΔT  = ( 13 + 8 ) kJ/molC = 21 kJ/molC</b> az egyatomos nemesgázok esetében,
<br> 
-  <b> C<sub>2</sub> = Q / ΔT = ( ΔE<sub>b5/2</sub> + W<sub>szétnyom</sub> ) / ΔT  = ( 21 + 8 ) kJ/molC = 29 kJ/molC</b> a kétatomos gázok esetében, és
<br>
-  <b> C<sub>s</sub> = Q / ΔT = ( ΔE<sub>b6/2</sub> + W<sub>szétnyom</sub> ) / ΔT  = ( 25 + 8 ) kJ/molC = 33 kJ/molC</b> a sokatomos gázoknál. 
<br><br>
Mindhárom esetben azonban a térfogat növelésére szánt munka azonos: <b> C<sub>W</sub> =  W<sub>szétnyom</sub>  / ΔT  = 8  kJ/molC = R<sub>e</sub> </b>, a Regnault állandót adja.










