

Figyelembe véve hogy:
<br><br>
<b> p = 1 Atm = 100 000 N/m<sup>2</sup></b> atmoszferikus nyomáson
<br>
<b> T = 0 C = 273 K</b> fok hőmérsékleten
<br>
<b> V = 22 l = 0.022 m<sup>3</sup></b> térfogatot foglal el egy mól mennyiségű gáz  ( Avogadro számú gázrészecske, <b> N<sub>A</sub> =  6*10<sup>23</sup> </b>) 
<br>
fajlagos belső energiája:
<br><br>
<b>  R = p * V / T = 100 000 N/m<sup>2</sup> * 0.022 m<sup>3</sup> / 273 K =  2200 Nm /273 K = 8.3 J/K</b> 
<br>
( ahol <b>R</b> a Regnault féle gázállandó egy mól mennyiségű, Avogadro számú gázrészecskére), és felhasználva a 
<br><br>

 <b>p * V = n * R * T </b> összefüggést <b> n </b> mol mennyiségű gázra, valamint az előző leckében nyert  
<br>
<b>p * V =  m * v<sup>2</sup> / 3 </b>  összefüggést,  kapjuk hogy az  <b>n</b> mólnyi gáz, avagy <b>N</b> darab ( <b>N = n * N<sub>A</sub> </b>) gázrészecske mozgási energiája: 
<br>
<b>E<sub>k</sub>  = m * v<sup>2</sup> / 2 = 3/2 n * R * T  = 3/2 N * k * T</b>
<br><br>
ahol <b>k = R / N<sub>A</sub> = 8.3 / (6*10<sup>23</sup>) J/K = 1.4 *10<sup>-23</sup> J/K </b> a Boltzmann konstans,
	<br><br>
	mely megmutatja, hogy egy gázrészecske mozgási energiája  <b>E<sub>k1</sub> =  m<sub>1</sub> * v<sup>2</sup>  / 2 = 3/2  k * T</b>, ahol  <b> m<sub>1</sub>  = m / N </b> .
<br><br><br>

Általánosítva a fenti egyatomos részecskékre kidolgozott képletet a  két- és háromatomos részecskéket is tartalmazó gázokra:
<br><br>
<b>E<sub>k1</sub> = f/2  k * T</b>, vagy N részecskéből álló gáz esetén <b>E<sub>k</sub>   = f/2 N * k * T</b>, ahol <b> f </b> a szabadsági fokok száma,
<br>
<b> f = 3 </b>  az egyatomos három irányban mozgó részecskék esetén, 
<br>
<b> f = 5 </b> a kétatomos három irányban mozgó és két tengely körül forgó részecskék esetén, valamint 
<br>
<b> f = 6 </b> a háromatomos három irányban mozgó és három tengely körül forgó részecskék esetén.
<br><br>
Magyarázat:  
<br>
-száz részecskéből csak egy részecske rendelkezzen mozgási energiával, a többi nyugodjon: az ütközések következtében rövid időn belül ez az energia egyenletesen eloszlik (! - ekvipartició elve - !) az összes részecske között.
<br>
- a száz részecske mozogjon csak az <b>x</b> irányban, az <b>y</b> és <b>z</b> irányokban legyen nyugvás: az ütközések következtében rövid időn belül ez az energia egyenletesen eloszlik az <b>x</b>, <b>y</b> és <b>z</b> irányokba is (! - ekvipartició elve - !), ezért <b> f = 3 </b> .
<br>
- a száz kétatomos részecske mozogjon csak az <b>x</b>, <b>y</b> és <b>z</b> irányokban, de az  <b>y</b> és <b>z</b> forgástengelyek körül legyen nyugvás: az ütközések következtében rövid időn belül ez az energia egyenletesen eloszlik az <b>x</b>, <b>y</b> és <b>z</b> irányokba való mozgás és <b>y</b> és <b>z</b> forgástengelyek körüli pörgésre  is (! - ekvipartició elve - !), ezért <b> f = 5 </b> .
<br>
- a száz háromatomos részecske mozogjon csak az <b>x</b>, <b>y</b> és <b>z</b> irányokban, de az  <b>x</b>,  <b>y</b> és <b>z</b> forgástengelyek körül legyen nyugvás: az ütközések következtében rövid időn belül ez az energia egyenletesen eloszlik az <b>x</b>, <b>y</b> és <b>z</b> irányokba való mozgás és <b>x</b>, <b>y</b> és <b>z</b> forgástengelyek körüli pörgésre  is (! - ekvipartició elve - !), ezért <b> f = 6 </b> .
<br><br><br>

A hőtan I. főtétele kimondja, hogy a gáz belső energiájának változása egyenlő a gáz által leadott vagy felvett munka és hőenergia összegével: 
<br><br>
<b> ΔE<sub>b</sub>  =  W + Q </b>, ahol a belső energia valóban a gázt alkotó részecskék mozgási energiájából ered, 
<br><br>
melyet a Kelvin fokban mért abszolút hőmérséklettel jellemezünk: <b> ΔE<sub>b</sub>  =  ΔE<sub>k</sub>  = f/2 N * k * ΔT </b>.
<br><br><br>

Így például nyomjuk össze a  <b> V </b> térfogatú gázt térfogata felére: az összenyomásához <b> F </b> erőt kell kifejtenünk, és mivel <b> s </b> szakasszal nyomtuk összébb, a gázon végzett munka: <b> W = F * s </b>. Mivel ezzel energiát adtunk a gáznak, ezt a részecskéknek át kell venniük, mozgásuk gyorsabb lesz, ezzel a gáz hőmérséklete lesz nagyobb (adiabatikus folyamat). 
<br><br>
Amennyiben azonban megengedjük, hogy a gáz a többlet energiát leadja a környezetének, olyannyira, hogy a hőmérséklete semennyit sem változzon (izoterm folyamat), akkor csak a nyomás fog megduplázódnia a felére zsugorodott térfogat miatt ( <b> p * V = const.</b>, Boyl-Mariott törvény) .
<br><br>
A gáz állandó nyomáson való tartásával (izobár folyamat), például lufi melegedése a tűző napon a Kelvinben mért hőmérsékletnövekménnyel arányos térfogatnövekményt fog eredményezni ( <b> V / T = const.</b>, Gay-Lussac első törvénye) .
<br><br>
A gáz állandó térfogaton való tartásával (izochor folyamat), például kukta melegedése a tűzhelyen a Kelvinben mért hőmérsékletnövekménnyel arányos nyomásnövekményt fog eredményezni ( <b> p / T = const.</b>, Gay-Lussac második törvénye) .























