
 <b> <br>
 - Egy pontszerű test akkor marad egyenes pályán ha a rá ható erő hatásvonala egybeesik a pálya egyenesével. </b>
 <br><br> 
 - Ha a testre ható erő (mely lehet több erő eredője) nulla nagyságú, akkor a test tehetetlensége folytán egyenesvonalú egyenletes mozgással folytatja útját, vagy nyugalomban marad, ha a testet a testtel megegyező sebességű vonatkoztatási rendszerből nézzük.
<br> <br>
 <b> - Egy pontszerű test akkor gyorsul egyenletesen egyenes pályán ha a rá ható erő változatlan nagyságú és hatásvonala egybeesik a pálya egyenesével. </b>
 <br><br>
 - Az egyenletes körmozgást fenntartó erőhatás állandó nagyságú, mindig merőleges a test mozgására és a test által bejárt kör középpontja felé mutat ( a <b> centripetális erő </b> merőleges a kerületi sebességre), nagysága pedig: 
 
<br>
<b> F<sub>cp</sub> = m * a<sub>cp</sub> = m * Δv/dt </b> , ahol a <b> Δv </b> sebességváltozás mindig merőleges a test pályájára:
<br>Δv = v * Δϕ = v * Δs/r  </b>, így a pályára merőleges centripetális gyorsulás:
   <br>
  <b>  a<sub>cp</sub> = Δv/Δt = v * Δϕ/Δt = v/r * Δs/Δt = v<sup>2</sup>/r </b>,  ahol <b> Δϕ </b> az elfordulás  <b> Δs </b>  keskeny ívszeletének nyílásszöge.
