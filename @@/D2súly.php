Mindennapi szóhasználatban azt mondjuk, hogy egy liter tej súlya 1kg, ami nem igaz:
<br>
- egy liter tej <i><b> tömege </i></b> 1kg, súlya pedig az az erő amelyikkel az 1kg tömeg nyomja az asztal felületét a Föld gravitációs vonzása következtében, ami 10N nyomóerő, azaz egy liter tej súlya 10N
<br>
- a Holdon 1kg tömegű literes tej ötször kisebb erővel nyomja az azstal felületét, ami 2N nyomóerő, azaz egy liter tej súlya 2N
<br>
- kint a világűrben, súlytalansági állapotban az 1kg tömegű literes tej nulla erővel nyomja az asztal felületét, lebeg felette, tehát egy liter tej súlya NULLA!
<br>
- és gondolkozzunk el: az orvos megkér minket, hogy maradjunk nyugalomban, amíg megméri testsúlyunkat. Amikor elrugaszkodunk hogy felugorjunk, a mérleg 200kg tömegnek megfelelő 2000N "súlyt" mér 50kg>500N helyett, majd visszazuhanáskor (legyen a mérleg ultrakönnyű szupermodern piezoérzékelős fólia) a lábunkra ragasztott mérleg NULLA súlyt, azaz súlytalanságot mér!
