A világ jellemzője az idő ("<b>t</b>") és a tér ("<b>x</b>"). Egy objektum a megfigyelőhöz viszonyítva lehet nyugalomban, de lehet mozgásban is: amennyiben térbeli "<b>x</b>" helye "<b>t</b>" idő elteltével megváltozik azt mondjuk:  a tárgy "<b>v = x / t</b>" sebességgel <i><b>mozog</i></b>.
<br><br>
A mozgás (vagy nyugvás) viszonylagos, nem mérhető meg, nem állapítható meg, nincs kihatással a tárgyak, történések, folyamatok létezésére, lefolyására. 
<br><br>
A világot alkotó objektumok (tárgyak) hatnak egymásra. Ez a kölcsönhatás pontról-pontra terjed, ami logikus következménye: véges sebességgel (a fény sebességével), mely ugyanaz  bármely mozgási rendszerből is néznénk (minden vonatkoztatási rendszer egyenrangú). A fény sebességének állandósága ellentmondásmentesen illeszkedik abba a keretbe, hogy nem létezik abszolút értelembe vett mozgás vagy nyugalom. Másként nem is lehetne...
<br><br>
A fenti megfontolások alapján a nyugvó rendszerben mért történés "<b>t<sub>0</sub></b>" ideig létezik (mely idő alatt a fényóra fénysugara "<b>c t<sub>0</sub></b>" utat tesz meg).  A  "<b>v</b>"  sebességgel mozgó rendszerben ez úgy mérhető, hogy ugyanaz a történés "<b>t</b>" ideig tart  (mely idő alatt a fényóra fénysugara "<b>c t</b>" utat tesz meg),  és a történés  "<b>v t</b>" elmozdulást szenved, az alábbi összefüggésnek eleget téve:

<br><br>
<b>c<sup>2</sup> t<sub>0</sub><sup>2</sup> = c<sup>2</sup> t<sup>2</sup> - v<sup>2</sup> t<sup>2</sup>  </b>
<br><br>
(Pythagoras tétele alapján a  "<b>t<sub>0</sub></b>" idő alatt merőlegesen felszaladó fénysugár ugyanazt  a történést írja le mint a  "<b>v</b>"  sebességgel menekülő menyezetet  "<b>t</b>" ideig üldöző ferdén haladó fénysugár)
<br><br>
Továbbá, mély sejtéssel az idő ( "<b>t</b>" ) azonosítható az  anyaggal ("<b>m</b>"):

<br><br>
<b>c<sup>2</sup> m<sub>0</sub><sup>2</sup> = c<sup>2</sup> m<sup>2</sup> - v<sup>2</sup> m<sup>2</sup>  </b>
<br><br>
Egyszerű átalakítások útján megkapjuk az ismert összefüggést, hogy nagy sebességgel haladó test tömege nagyobb lesz (azonos mértékben mint az idő megnyúlása):
<br><br>
<b>m<sub>0</sub><sup>2</sup>  = m<sup>2</sup>  * (1 -  v<sup>2</sup> / c<sup>2</sup> )<br><br>
m<sub>0</sub> = m * (1 -  v<sup>2</sup> / c<sup>2</sup> )<sup>1/2</sup>
<br><br>
m = m <sub>0</sub> / (1 -  v<sup>2</sup> / c<sup>2</sup> )<sup>1/2</sup><br><br></b>
<br><br>
A test relativisztikus energiája (<b>E = m c<sup>2</sup>  </b>), és impulzusa (<b>P = m v  </b>) által a világ <i><b>idő-tér</i></b> leírásán túl megkapjuk a világ <i><b>energia-impulzus</i></b>vetületét is:
<br><br>
<b>E<sub>0</sub><sup>2</sup> = E<sup>2</sup> - P<sup>2</sup> c<sup>2</sup> </b>


