  <br><br>
  Az IMPULZUSMEGMARADÁS törvénye az az alapelv, mely magába foglalja Newton három törvényét:

  <br>
  - az INERCIA (tehetetlenség) törvénye az impulzusmegmaradás következménye, tömeg a mértéke,
  <br>
  - az ERŐHATÁS az impulzus/mozgásmennyiség egységnyi időben történő megváltozásának mértéke<b> F = ΔI/Δt </b>("Δ" a változás jele),
  <br>
  - a HATÁS-ELLENHATÁS a két kölcsönható test együttes impulzusának megmaradásán alapul<b> ΔI<sub>1</sub> + ΔI<sub>2</sub> = 0</b>, tehát  <b>dΔI<sub>1</sub> = -ΔI<sub>2</sub></b> .
  <br><br>
  Vegyük észre, hogy Newton törvényei (1680-as évek) az impulzusmegmaradás elvére épülnek (és fordítva: Newton törvényei az impulzusmegmaradást sugallják): 
  <br>
  - száz év kellett ahhoz, hogy megfogalmazzák a tömegmegmaradás törvényét (Lomonoszov-Lavoisier-törvény 1780-as évek), 
  <br>
  - az energiamegmaradás törvényét hatvan évre rá fogalmazták meg (Joule, Carnot, Clapeyron és Helmholtz munkássága 1840-es évek), 
  <br>
  - további hatvan évre rá 1905-ben Albert Einstein speciális relativitáselméletében megfogalmazta az energia és tömeg ekvivaleciáját:<b>  E = m*c<sup>2</sup></b> , valamint a négyesimpulzus, azaz a nyugalmi tömeg megmaradásának elvét (megfogalmazva az energia és impulzus kapcsolatát a négyesimpulzus és nyugalmi tömeg összefüggésében),

<br>
 - majd tíz évre rá 1915-ben Emmy Noether a tér-idő transzlációs szimmetriájához köti az impulzus-  és energiamegmaradás törvényét.  
