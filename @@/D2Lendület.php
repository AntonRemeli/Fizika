  A tér-idő szimmetriából (a világűr bármely pontjára ráfoghatjuk, hogy az a világ közepe, és bármilyen gyorsan mozgó vonatkoztatási rendszerre ráfoghatjuk, hogy valóban ő van nyugalomban) és az anyag létezésének feltételezéséből (megtapasztaltuk, hogy a testek tehetetlenek, aminek mértéke az anyag mennyisége, azaz a test tömege) következik, hogy:
<br>
