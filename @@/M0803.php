További érdekességként még megvitatandó a -3C hideg (300W/m<sup>2</sup> feketetest-hősugárzás) belső szigetelés nélküli aluminiumfalú hűtőszekrény. A külső 17C meleg környezet 400W/m<sup>2</sup> hősugárzásából a tükröző aluminiumfal csak 1%, azaz 4W/m<sup>2</sup> hősugárzást fog elnyelni, és mivel külső felülete -3C hideg, 3W/m<sup>2</sup> (=300*1%) hősugárzást   ki fog sugározni. A a tükröző aluminiumfelület nettó energiafelvétele 4-3=1W/m<sup>2</sup> lesz. A 80W/m<sup>2</sup> (=20x4) hőátadást rövid időre felejtsük el (hagyományos szigetelés szükséges ilyen magas hőátadás kiküszöbölésére). <br><br>

Amennyiben a hűtőszekrény külső oldalát fehérre festjük, úgy az a környezeti hősugárzás 90%-át el fogja nyelni, ami 360 W/m<sup>2</sup> . Mivel külső fala most is -3C hideg, úgy a 300W/m<sup>2</sup> 90%-át fogja kisugározni, ami 270W/m<sup>2</sup> . Tehát jelentős, 90W/m<sup>2</sup> lesz a beáramló hő mennyisége az 1W/m<sup>2</sup> helyett, ami kilencvenszer több. A tükröző aluminiumfalú hűtőszekrényt ebből kifolyólag nem szabad fehérre festeni (de ha már befestettük, akkor lehet akár fekete is)!
<br><br>

A téves bevonat választásának következménye kellő vastagságú szigeteléssel eltusolható, de nem küszöbölhető ki:<br>

- 10cm nikecell belső hőszigeteléssel (0.4W/m<sup>2</sup>K hővezetés) a tükröző aluminiumfalú hűtőszekrény külső felülete -3C helyett 15C lesz (csak két fokkal hidegebb a környezettől), a tükröző aluminiumfelület sugárzása 3.9W/m<sup>2</sup> lesz (15C feketetest sugárzása 390W/m<sup>2</sup>), a 17C környezet sugárzása 400W/m<sup>2</sup> , amiből a tükröző aluminiumfelület 4W/m<sup>2</sup> energiát képes elnyelni, így  nettó sugárzási energiafelvétele 4-3.9=0.1W/m<sup>2</sup> lesz. <br>
 - a fehérre festett 10cm nikecell belső hőszigeteléssel bélelt hűtőszekrény külső felülete -3C helyett 16C lesz (egy fokkal hidegebb a környezettől), a fehér felület sugárzása 355W/m<sup>2</sup> lesz (=395-10%) , a 17C környezet sugárzása 400W/m<sup>2</sup>,  amiből a fehér felület 360W/m<sup>2</sup> energiát képes elnyelni (=400*0.9), így  nettó sugárzási energiafelvétele 360-355=5W/m<sup>2</sup> lesz. <br><br>
 
 A konvektív hőátadás figyelembevételével: <br>
 - 2C hőmérsékletkülönbség  8W/m<sup>2</sup> hőátadást eredményezne a tükröző aluminiumfelület esetében,<br>
 - 1C hőmérsékletkülönbség  4W/m<sup>2</sup> hőátadást eredményezne a fehér bevonatú hűtőszekrény esetében, azonban hozzáadódik még 5W/m<sup>2</sup> hőveszteség hősugárzás útján.<br><br>
 
  A fent kifejtettek alapján megállapíthatjuk, hogy a 10cm kőzetgyapot belső hőszigeteléssel rendelkező hűtő NEM lesz kevésbbé hatékony, ha tükröző aluminiumfalát fehérre festjük (felszíne azonban egy fokkal melegebb lesz) . 
  
  <br><br>
  
  A szigetelés nélküli esetben úgy a tükröző meg a fehér felület is -3C hideg, így mindkét esetben a vele érintkező meleg levegő áramlása 80W/m<sup>2</sup> energiát visz el (80=20*4), azonban, amíg a tükröző aluminium nettó hősugárzáselnyelése 1W/m<sup>2</sup>, a fehér bevonat nettó hősugárzás- elnyelése 90W/m<sup>2</sup> lesz:<br>
  - amíg a tükröző aluminiumfalú hűtő hőfelvétele 80W/m<sup>2</sup> körüli értéket vesz fel, addig<br>
  - a fehérre festett hűtő 70%-kal több, 135W/m<sup>2</sup> hőenergiát fog felvenni!
  <br><br>
  
   

  A fent kifejtettek alapján megállapíthatjuk, hogy a 10cm kőzetgyapot belső hőszigeteléssel rendelkező sütő vagy hűtő NEM lesz kevésbbé hatékony, ha tükröző aluminiumfalát fehérre festjük. Minél vastagabb a hőszigetelés, annál kisebb lesz a külső falfelület és a környezet hőmérsékletkülönbsége és ezzel párhuzamosan a hősugárzás és felületi légáramlásos hőátadás jelentősége is eltörpül.<br><br>
