<br>

Aristoteles és a többi görög filozófus nagyon ellenséges volt
Demokritos tanítása iránt. Szerintük minden létező a világon
tüz, víz, levegő és föld elegyéből van felépítve. Így feledésbe
is merült az atomisztikus elmélet...<br>

<br>

Több mint kétezer év eltelte után, a XVIII. század végéig
felgyülemlett kémiai ismeretek alapján egy angol tanár aki maga
is kísérletezett, John Dalton újraélesztette Demokritos
atomelméletét <a href="http://www.sulinet.hu/tlabor/kemia/szoveg/k11.htm" target="_blank"><img alt="Dalton atomelmélete" title="Dalton atomelmélete" src="<?php  echo $hF;?>@/04tudomany.jpeg" border="0"></a>&nbsp; 1803-ban. A következő posztulátumokat fogalmazta meg:<br>

<br>

(1) Az elemek oszthatatlan kis részecskékből állnak (atomok).<br>

<br>

(2) Egyazon elemnek minden atomja azonos; különböző elemek
különböző típusú atomokkal rendelkeznek.<br>

<br>

(3) Az atomokat nem lehet létrehozni vagy elpusztítani.<br>

<br>

(4) ’Vegyült elemek’ (azaz vegyületek) képződnek, amikor
különböző elemek atomjai egyszerű arányok szerint kapcsolódnak
’vegyült atomokat’, (azaz molekulákat) létrehozva.&nbsp; <a href="http://www.tankonyvtar.hu/hu/tartalom/tkt/oxford-typotex-kemiai/ch01s04.html" target="_blank"><img alt="Dalton kémiai elmélete" title="Dalton kémiai elmélete" src="<?php  echo $hF;?>@/04tudomany.jpeg" border="0"></a><br>



