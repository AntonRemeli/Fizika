 Induló vagy fékező emelőben,  induló vagy fékező vagy kanyarodó járműben nyugvó testekre az őket megtartó felület vagy fonál  a járművel való együttmozgásra, azaz "együttnyugvásra" kényszeríti egy látszólagos tehetetlenségi erővel szemben.
 <br><br>
 Valójában azonban: a test tehetetlensége folytán az őt gyorsulásra kényszerítő testre ellenerőt fejt ki.
 <br><br>
 De amennyiben a gyorsuló járműben lévő testre azt akarjuk mondani, hogy nyugalomban van, akkor a megtapasztalt, testre ható erőhöz (ülés támlája nyomja a hátunkat, a körhinta centripetális erőhatással körmozgásra kényszerít minket) egy fiktív tehetetlenségi erőt kell párosítani úgy, hogy eredőjük nulla legyen, aminek köszönhetően a test "nyugalmi" (valójában "együttgyorsulási") állapotában marad.
