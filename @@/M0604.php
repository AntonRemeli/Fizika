<br>
Azonban azok a speciális festékek, melyek elnyelőképessége nagyobb a T+dT
hőmérsékleten mint sugárzóképességük T hőmérsékleten, kénytelenek
lesznek T hőmérsékletüket némileg megemelni&nbsp; T+ddT -re ahhoz, hogy
leadhassák az általuk elnyelt többletenergiát ( az áramlásos és
vezetéses hőveszteségeket első közelítésben változatlannak tekintjük a T és T+ddT
hőmérsékletű felületek mentén), az így megnövekedett hősugárzás T+ddT hőmérséklet
negyedik hatványával lesz arányos, ami kicsi ddT esetében
(T+ddT)<sup>4</sup>=T<sup>4</sup>*(1+4*ddT/T)
lesz ( ugyanis ddT=3C csak 1% a T=290K-hez viszonyítva).<br>
