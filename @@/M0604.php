fala 400+40W/m<sup>2</sup>
hőenergiát vesz fel, 400-at visszasugároz (17C fokon kialakul egy belső zárt sugárzási tér, a falfelület minőségétől függetlenül feketetest sugárzási jellemzővel), a hősugárzótól kapott 40W/m<sup>2</sup>
energiát elvezeti a kültérbe (a fűtőtest úgy van beállítva, hogy pont kompenzálja a falak hőveszteségét).&nbsp;&nbsp; <br>
<br>


Penészgátló csodafesték alkalmazásával a belső falfelület 
400+(40+4%)W/m<sup>2</sup> hőenergiát vesz fel, 17C-on 400W/m<sup>2</sup>
hőenergiát visszasugároz, a hősugárzótól kapott 40+4%=41.6W/m<sup>2</sup> energiából 40W/m<sup>2</sup>hőt elvezet a kültérbe, 1.6W/m<sup>2</sup> pedig 0.4%-kal megnöveli a visszasugárzást a szobába (0.4%=1.6/400), ami 0.4%=4ddT/T alapján ddT=0.3C (=0.4%*T/4=0.1%*290=0.29=0.3) fokkal megemeli a csodafesték felszíni hőmérsékletét. <br><br>

Ha a belső falfelület 17C helyett most 17.3C, akkor a hővezetés 0.3/20=1.5%-kal
megnövekszik 40.6W/m<sup>2</sup>-re. Mivel második közelítésben&nbsp; a
hősugárzótól kapott 41.6W/m<sup>2</sup>
energiából 40.6W/m<sup>2</sup> elmegy a kültérbe, a visszamaradó 1W/m<sup>2</sup>&nbsp; 0.25%-kal megnöveli a visszasugárzást a szobába, ami 0.25%=4ddT/T alapján most ddT=0.2C  (=0.25%*T/4=0.06%*290=0.18=0.2) fokkal emeli a fal felszíni hőmérsékletét. Így a hővezetés csak 0.2/20=1%-kal, azaz 0.4W/m<sup>2</sup>-rel lesz nagyobb, a visszasugárzásra maradó  1.2W/m<sup>2</sup> 0.3%-kal lesz nagyobb a 400W/m<sup>2</sup>-hez képest, a fal hőmérséklete 0.23C fokkal magasabb....<br>
