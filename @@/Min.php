De vajon elsajátítható-e "egyszerűen" a tudomány? 
A repülőgép ötmillió alkatrészből van felépítve és mégis egyszerű?
Megérteni könnyű: test, szárnyak, hajtómű. Megépítése tiszteletet követel, mint megannyi emberi és emberen túlmutató . . .<br><br>

Ebben a hagyományostól eltérő, infokommunikációs technológia (IKT-ICT) eszköztárát kiaknázó "tankönyvben" a lehető legnagyobb mértékben mellőzzük a matematikát, az ismertetett képleteket a lehető legkisebb mértékre szorítjuk vissza (tudatában léve annak, hogy minden egyes képlet megtizedeli az olvasótábort), inkább a megfigyelésre helyezzük a hangsúlyt (ami gyakran csak egy elképzelt megfigyelés, úgynevezett "gondolatkísérlet"), és amikor az összefüggéseket kell feltárnunk (ami lényegében egy képlet, azaz matematika), nem elégszünk meg a "meztelen" képlet ismertetésével (ahogy a hagyományos tankönyvekben szokásos, természetesnek véve, hogy a tanulni akaró vagy tanulásra kényszerített alany ezzel már minden lehetséges tudást elsajátított), hanem azt "kibeszéléses" módszerrel minden fontos szempontból "végigrágjuk", képzeletbeli megfigyelések sorozatával mindennapi megfigyelések szintjére hozzuk, ami által a taglalt jelenség úgymond megszokott, könnyen megérthető lett...<br><br>

Ennek az eljárásnak csupán egy hátránya van: elég hosszadalmas egy-egy gondolatkísérlet végigolvasása, az aritmetikai (számszerű) összefüggések nyomon követése (a fizika gyöngyszemét, az absztrakt analitikai képletekbe összesűrített gondolkodásmódot  mellőzzük).  De hát a valós életben sem szeretjük túlságosan használni az agyunkat: inkább hagyjuk, hogy a dolgok százszor is megtörténjenek velünk: egy idő után úgyis "megértjük" (és már nem az a fárasztó tudatos gondolkodás vezet minket az életünkben, hanem intuitív megérzésünkre bízzuk magunkat, az agyunk a háttérben mindent megold helyettünk). <br><br>

Az igazi tudás az intuitív tudás, az pedig tapasztalatokból fakad, megfigyelések tömkelegéből: ezért az Olvasót arra biztatjuk, hogy ne rettenjen vissza attól a sok kis fekete hangyától amit írott szövegnek nevezünk, vegyen mély lélegzetet és merüljön alá a Fizika csodálatos világába!
