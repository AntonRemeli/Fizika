Tükörsíma fémes felületek visszaverik a napsugárzás 95%-át, a hősugárzást még jobban, akár 99%-ban  visszatükrözik. Azonban, amennyiben felmelegedtek, akkor nagyon nehezen, a feketetesthez viszonyított 1% hatékonysággal  adják le a hőenergiát. <br><br>

A fehér bevonat 60-80%-ban tükrözi a napsugárzást, a környezeti hősugárzást azonban csak 10%-ban veri vissza. Ez azonban azt jelenti, hogy fehér felületek 90% hatékonysággal elnyelik a környezeti hősugárzást, és amennyiben felmelegedtek, ugyancsak 90% hatékonysággal kisugározzák magukból a hőenergiát. <br><br>

Készítsünk egy olyan egyköbméteres hűtőszekrényt, mely oldalai 99%-ban visszaverik a környezet hősugárzását, teteje pedig legyen olyan ultrafehér bevonat, mely 99%-ban visszaveri a napsugárzást és mindennemű (nap- és környezeti) hősugárzást, azonban a kozmikus ablak 8-14 μm  tartományában legyen koromfekete (magas, 99%-os emisszivitás, tehát csak 1%-os reflektivitás): <br> 
- a hűtődoboz 5+1 tükörreflektáló oldala 5*400W/m<sup>2</sup> környezeti sugárzás (17C a környezet hőmérséklete) és 1000W/m<sup>2</sup> napsugárzás éri, aminek 99%-át visszatükrözi és így 20+10W energiát elnyel,<br> 
- mivel a  doboz a környezettel azonos hőmérsékleten van az öt oldal 5*400W/m<sup>2</sup>  1%-át, 20W energiát vissza is sugároz a környezetbe, a fedlap pedig a kozmikus ablak 8-14 μm  tartományában 200W hőenergiát kisugároz az űrbe (mert 290K=17C hőmérsékleten a feketetest sugárzási energiájának fele  a 8-14 μm termális  tartományban van).
 <br>  <br> 
 Az áram nélküli hűtőszekrényünk elekezd erőteljesen hülni, és mindaddig csökkeni fog hőmérséklete amíg a világűrbe való kisugárzása nem egyenlítődik ki az elnyelt hősugárzással: <br> 
 -110C fokra hülne akkor, ha hűtőszekrényünk körül vákuum volna (nincs hőáramlás, csak környezeti hősugárzás),  <br> 
 - a Föld légkörében 16W/m<sup>2</sup>K konvektív hőátadási tényezővel számolva  hűtődobozunk csak 2C fokkal lesz hidegebb környezetétől, ha oldalfalait nem látjuk el belső hőszigeteléssel,  <br> 
 - 10cm nikecell szigetelés alkalmazásával 17-ről 7C fokra hülne le a hűtőszekrény belseje (10C fokkal lesz hidegebb környezetétől) és szigetelés nélküli fedlapja (hogy hatékonyan kisugározhassa a belső hőt). A fedlap 150W energiát sugározna az űrbe, amennyiben olyan karimával látjuk el a fedlapot, hogy megüljön rajta az általa lehűtött levegő, és így a fedlap légáramlásos hőátadása 160W helyett 120W körülire csökkenne, a hővezetés pedig 20W volna az öt szigetelt oldal felé.
 <br> 
 - kánikulában azonban ugyanez a nikecellal szigetelt szekrény 33 fokról 19C fokra hülne le (14C fokkal lesz hidegebb környezetétől, kánikulában hatékonyabb!!). A fedlap 205W energiát sugározna az űrbe, amennyiben olyan karimával látjuk el a fedlapot, hogy megüljön rajta az általa lehűtött levegő, és így a fedlap légáramlásos hőátadása 224W (=14x16) helyett 180W körülire csökkenne, a hővezetés pedig kb. 28W volna az öt szigetelt oldal felé.
 <br> 
 - mivel azonban 99% napenergiareflexiót még nem értünk el (laborban elért, még fejlesztés alatt 97% a maximum), kereskedelmi forgalomban pedig 85% a legmagasabb napenergiareflexió, úgy 150W elnyelt napenergiával kell számolnunk. Ebből kifolyólag az űrbe effektíve csak 55W lesz kisugározva, így 33 fokról 31C fokra hülne le a hűtőszekrény belseje, 32W menne hőátadásra (védőkarimát elhagytuk), 4W pedig hővezetésre az öt nikecell-bélelt oldalfalon át. Szelektív hősugárzás a termális infratartományban (kozmikus ablak) nem lényeges, mert amennyit az ettől szélesebb infratartományban kisugároz, ugyanannyit vissza is kap a légkörből.<br> <br> 
 
