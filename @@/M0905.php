 "A nanotechnológiai eljárással létrehozott különleges hőszigetelőkben az anyagok nanoméretű struktúrája miatt, a hőtranszport folyamatok, csak fékezetten tudnak megvalósulni. Úgy is mondhatnánk, hogy a nanoméretű pórusossággal rendelkező anyagokban már nem a megszokott módon érvényesülnek a hő terjedésével kapcsolatos törvényszerűségek. Az ilyen hatékony hőszigetelő anyagok hővezetési tényezője akár λ = 0,001 W/mK érték is lehet."<br>
 
