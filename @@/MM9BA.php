
  <br> 
Eddig feltételeztük, hogy a pontszerű test bármerre elmozdulhat valamilyen erő hatására. Azonban, ha akadály állja útját (egy fal, vagy padló, vagy lánc mely leláncolja) a test kényszermozgásra kárhozatik.
<br> <br> 
  Az akadály által kifejtett kényszererő mindig merőleges a kényszerítő felületre (vagy a megfeszült lánc/fonál mentén hat) és mindig pont olyan nagyságú lesz mint a testre ható szabaderő (a felület pont annyit horpad be hogy ellensúlyozza a test nyomását, a fonál pont annyit nyúlik meg, hogy ellensúlyozza a húzóerőt), de ellentétes irányú, ezzel semlegesítve a szabaderő felületre merőleges (vagy fonálmenti) komponensét.
  <br> <br> 
  A dinamika alapegyenlete ebben az esetben <b> m*a = F + K </b> alakban írható fel.
  <br> 
-  Nyugvó, vagy egyenletes sebességgel haladó emelőben ez a kényszererő a testre ható gravitációs vonzást semlegesíti: <b> K = -F<sub>g</sub> </b> , gyorsulás nincsen.
<br> 
- Induló, vagy fékező emelőben a kényszererő vagy ráad a gravitációs erőre (indulás felfelé, fékezés lefelé menet), vagy elvesz belőle (indulás lefelé, fékezés felfelé menet), így a test gyorsulása: <b> m*a = F<sub>g</sub> + K </b> 
  <br> <br> 
  Lejtőre helyezett testre ható gravitációs erő a lejtőre merőleges komponensre bontható (mely hatását a <b>K = -F<sub>gm</sub></b> kényszererő semlegesíti), a lejtővel párhuzamos erőkomponens pedig a test gyorsuló lecsúszását okozza <b>m*a = F<sub>gp</sub></b>. 
