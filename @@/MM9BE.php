Két test között a gravitációs vonzás arányos azok tömegével és fordítva arányos távolságuk négyzetével:
<br><br><b>
F<sub>g</sub> = f * m<sub>2</sub> * m<sub>2</sub> / r<sup>2</sup></b>
<br><br>
Ebből, a földfelszíni gravitációs gyorsulás g=10m/s<sup>2</sup> és Föld R=6000km sugara ismeretében kiszámítható a Föld <b>M</b> tömege:
<br><br><b>
m * g  = f * m * M / R<sup>2</sup></b> , amiből:
<br><br><b>
M = g * R<sup>2</sup> / f = 10N/kg * (6Mm)<sup>2</sup>/ 67 * 10<sup>-12</sup> Nm<sup>2</sup>/kg<sub>2</sub> = 360/67 * 10<sup>24</sup> kg = 6 * 10<sup>24</sup> kg
