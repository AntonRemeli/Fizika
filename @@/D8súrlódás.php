Amennyiben két test egymáson nyugszik, úgy tapadási súrlódási erő lép fel közöttűk:
<br>
- a tapadási súrlódási erő mindig egyenlő nagyságú és ellentétes irányú azzal az erővel, amellyel a testet meg akarjuk mozdídatni.
<br>
-  a tapadási súrlódási erő nagyobb, ha érdesebbek az érintkező felületek, és akkor is, ha azok nagyobb erővel nyomódnak egymáshoz. 
<br><br>
Amennyiben két test egymáson csúszik, úgy csúszási súrlódási erő lép fel közöttűk:
<br>
- a csúszási súrlódási erő mindig egyenlő nagyságú és ellentétes irányú azzal az erővel, amellyel a testet mozgatjuk.
<br>
- a csúszási súrlódási erő nagyobb, ha érdesebbek az érintkező felületek, és akkor is, ha azok nagyobb erővel nyomódnak egymáshoz.
<br><br>
Mind a tapadási, mind a csúszási súrlódási erő csökkenthető azáltal, hogy az érintkező felületeket símára csiszoljuk, és mégjobban, ha kenőanyaggal megolajozzuk az érintkező felületeket.
<br><br>
