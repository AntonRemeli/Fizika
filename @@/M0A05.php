Az <b>E = m c<sup>2</sup>  </b>  relativisztikus energia (ahol <b>m</b>  a relativisztikus "puffadó" tömeg) <b>v << c</b> esetében az <b> E<sub>k</sub> = 1/2 *m<sub>0</sub>v<sup>2</sup> </b> kinetikus energia megszokott képletét adja:
<br><br>
<b>
E = mc<sup>2</sup> <br>
E = m<sub>0</sub>c<sup>2</sup> / (1-v<sup>2</sup>/c<sup>2</sup>)<sup>1/2</sup><br>
E = m<sub>0</sub>c<sup>2</sup> * (1-v<sup>2</sup>/c<sup>2</sup>)<sup>-1/2</sup>  </b> ......  v << c esetében:<br><b>
E ~ m<sub>0</sub>c<sup>2</sup> * (1+1/2 * v<sup>2</sup>/c<sup>2</sup>)<br>
E = m<sub>0</sub>c<sup>2</sup> + 1/2 *m<sub>0</sub>v<sup>2</sup></b>
<br><br>
Megjegyzés: a hagyományos nemrelativisztikus fizika figyelmen kívül hagyja az objektumok nyugalmi  <b>E<sub>0</sub> = m<sub>0</sub> c<sup>2</sup>  </b>  energiáját, csak a kinetikus energiával számol.
<br><br>
Az <b> L  </b>  relativisztikus hosszúság  <b>v << c</b> esetében az <b>1/2 *L<sub>0</sub>v<sup>2</sup>/c<sup>2</sup> </b> hosszúságkontrakció (hosszúságrövidülés) képletét eredményezi:
<br><br>
<b>
L = L<sub>0</sub> * (1-v<sup>2</sup>/c<sup>2</sup>)<sup>1/2</sup>   </b>......  v << c esetében:<br><b>
L ~ L<sub>0</sub> * (1-1/2 * v<sup>2</sup>/c<sup>2</sup>)<br>
L = L<sub>0</sub> - 1/2 *L<sub>0</sub>v<sup>2</sup>/c<sup>2</sup></b>
<br><br>
Megjegyzés: a mindennapokban megfigyelt és mérhető hagyományos kinetikus energiával szemben a hosszúságkontrakció nem mérhető a  rendkívül nagy <b>c<sup>2</sup> =  10<sup>17</sup> m<sup>2</sup>/s<sup>2</sup></b> osztó miatt. 
<br><br>
Jobban mondva: nagyon is mérhető, mindennapjaink része, csak nem tudunk róla: az elektromágnes, az elektromos áram mágneses hatása valójában a mérhetetlenül kicsi hosszúságkontrakció mindennapi életben észlelhető megnyílvánulása: az elenyészően gyenge gravitációs kölcsönhatással szemben az elektromágneses kölcsönhatás rendkívül erős:  megszokott  <b> 1A </b> áramerősség  <b> 1C </b>  (1 Coulomb = 6 * 10<sup>18</sup> e<sup>-</sup> ) töltés mozgását jelenti (szerencsére ugyanannyi nyugvó pozitív töltésnek köszönhetően a vezeték semleges töltésű lesz).  <b> 1C </b> negatív és <b> 1C </b>  pozitív töltés elképesztő nagyságú <b>10<sup>14</sup>N</b> erővel vonzaná egymást 1cm távólságból. Ha most két párhuzamos egyméteres drótban   <b> 1C </b> áram folyna ugyanabban az irányban (az elektronok 1m/s sebességgel haladnak), akkor az elektronok nyugalmi rendszeréből nézve a visszafelé mozgó pozitív töltések hosszúságkontrakció következtébben <b> 10<sup>-17</sup></b> mértékben sűrűbben vannak mint ugyanabban a drótban nyugvó elektronok (elektronok mozgó koordinátarendszeréből nézve), így a drótban <b> 10<sup>-17</sup>C</b>  pozitív töltéstöbblet van, tehát a vezeték pozitív töltésű és vonza az átellenes drót <b>1C</b> elektronját:  <b> 10<sup>-17</sup> * 10<sup>14</sup>N = 10<sup>-3</sup>N = 1mN</b>, ha ezer menet állna ezer menettel szemben, akkor  <b> 1A </b> áram a két tekercset  <b> 1000N (!) </b> mágneses erővel vonzaná egymás felé (100kg súly felemelése) !
