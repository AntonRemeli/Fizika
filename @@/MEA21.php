Az első főtétel az energiamegmaradás törvényét fogalmazza meg a hőtanban.
<br><br>
A második főtétel azt mondja, hogy a hőenergia csak a hidegebb testek felé haladhat, fordítva nem.
<br><br>
A harmadik főtétel pedig azt, hogy az abszolút nulla hőmérséklet tetszőleges mértékben megközelíthető, da soha sem érhető el.

<br><br><br><br>

Nyomjunk össze egy levegővel telt tartályt térfogata felére. Ehhez munkát kell befektetnünk:
<br><br>
<b> W = F * s = F/A * A*s = p * V </b>
<br><br>
vegyük észre, hogy minél jobban összenyomódott a gáz, annál nagyobb nyomással áll ellen az összenyomó erőnek, tehát annál nagyobb erő szükséges. Ezért <b>  F </b> egy kiátlagolt erőt, <b>  p </b> egy  átlagos nyomást jelképez, melyek annál közelebb lesznek a valóshoz, minél kisebb a <b>  V </b> térfogatmegváltozás. 
<br><br><br>

Ezen összenyomási munka következtében megnövekedett a gáz nyomása és hőmérséklete is. Ez <b>  adiabatikus </b> állapotváltozás.
<br><br>
Engedjük meg, hogy az összenyomási  munka egy része kárba vesszen: az így keletkezett hő távozzon a környezetbe, csak a nyomás növekedjen kétszeresére a kétszer kisebb térfogatban. Ez így most már <b>  izoterm </b> állapotváltozás.
<br><br><br><br>

Most meg képzeljük el ennek ellenkezőjét, egy hengert és benne egy dugattyút, mely a nagy belső nyomás hatására meghajtja a gőzgép lendkerekét:
<br><br>
Fűtsünk jól alá, legyen nagy a nyomás. A kifelé táguló dugattyú munkát végez, csökken a gáz nyomása és hőméraséklete egyidőben ( <b>  adiabatikus </b> állapotváltozás) de egyidőben fűtsük tovább, hogy hőmérséklete mégsem csökkenjen (<b>  izoterm </b> állapotváltozás).
<br><br>
Amikor a nyomás kiegyenlítődött a kültérivel, megáll a dugattyú. Ahhoz, hogy helyére visszahúzódjon, anélkül, hogy erővel nyomnánk vissza (tehát külső munkavégzés nélkül), ezt megtehetjük a gáz hűtésével: a lehülő gáz összeesik saját magától....
