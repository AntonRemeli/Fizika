A korom és a tükrön túl létezik azonban egy harmadik anyag is: az üveg. Az átláthatóság (transzparencia) a tükrözés (reflexió) ikertestvére: egyik sem nyeli el a sugárzást, de amíg a tükör visszafordítja azt, addig az üveg irányváltozás nélkül továbbengedi a merőlegesen beeső fényt (egyéb szögek esetén fénytörés, irányváltás lép fel). Azonban az üvegnek van két igen fontos tulajdonsága:<br>
- az üveg csak a látható fény mikrométeres tartományában fényáteresztő, a hősugárzás nagyon hosszú hullámhosszán (kettő, tíz, száz mikrométer és azon túl) és az ultraubolyasugárzás nagyon rövid hullámhosszán (egytized, egyezred mikrométer és azon túl) koromfekete, fényelnyelő,<br>
- az üveg átlagosan 8%-ban a látható fényt is visszatükrözi. Általában minden anyag kb. 10% környékén tükrözi vissza a fényt, de diffúz, szórt módon, mivel azonban az üveg általában szabályos sík felületet alkot (a dunai mosott homok csillogó kvarclapocskái ugyanúgy), a ferde szögben ráeső párhuzamos fénnyalábot visszatükrözi (aminek felvillanása elárulja a karórát, szemüveget viselő rejtőző katonát). <br><br>

