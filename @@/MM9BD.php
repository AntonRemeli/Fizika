<br>
- a közegellenállás mindig egyenlő nagyságú és ellentétes irányú azzal az erővel, amellyel a testet mozgatjuk, és függ:
<br>
- a közeg sűrűségétől,
<br>
-a test mozgásirányra merőleges keresztmetszetétől (a kés élével vágjuk a kenyeret, nem a lapjával)
<br>
- a test közeghez viszonyított sebességétől (erős szél ellen nehezebb szaladni)
<br>
- a test alakjától (aerodinamikus, csepp alakú versenyautó, tengeralattjáró)
