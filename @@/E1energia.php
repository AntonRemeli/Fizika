Húzzunk szánkót: annál jobban elfáradunk, minél többen ülnek rajta (nagyobb súrlódási erő ellenében kell húzni), és minél hoszabb úton kell elhúzni. A befektetett munka nagysága: <br><br>

<b>W = F * s </b>. <br><br>

<b>F = m * g</b>  erővel tartjuk meg az <b>m</b> tömegű testet. Ahhoz hogy <b>h</b> magasságra felemeljük: <br><br>

<b>W = m *g *h</b>  munkát kell befektetnünk. <br><br><br><br>




<b>h = 1.8 m</b>  magasan van az <br><br>

<b>m = 1 kg</b> tömegű téglatest. <br><br>

<b>W = m *g *h = 1 kg * 10 m/s<sup>2</sup> * 1.8 m = 18 J </b> munkával emeltük fel ezt a testet a tesztpályára. Meredek lejtőn zuhanjon alá egy rugóstálcára. <br><br><br><br>



A tálca alatti rugó <b>D = 1 cm</b>-t nyomódik össze <b>F = 100 N </b>(10 kg súly) erő hatására:<br><br>

<b>F = d * D</b> , ahol <b>d = 100 N/cm</b> rugalmassági állandó, <b>D</b> pedig a megnyúlás/összenyomódás nagysága. <br><br>

Az első <b>D = 1 cm</b> összenyomódás <b>W = F * s = 100 N * 1/100 m = 1 J</b> munkát igényel,<br><br>

a második centiméter <b> F = 200 N </b> erő ellenében <b> W = 2 J</b> munkát igényel,<br><br>

a harmadik centiméter <b> F = 300 N </b> erő ellenében <b> W = 3 J</b> munkát igényel,<br><br>

....<br><br>

a hatodik centiméter <b> F = 600 N </b> erő ellenében <b> W = 6 J</b> munkát igényel.<br><br>

A átlagos erőhatás a rugóra <b> F = 0 N </b> és <b> F = 600 N</b> között <b> F = 300 N</b>, így a <b>D = 6 cm</b> összenyomásra fektetett munka:

<b>W = F * D = 300 N * 6/100 m = 18 J</b>. <br><br><br><br>


Az összenyomott rugó feldobja a téglatestet <b> h = 1.8 m</b> magasra a második lejtőn, amelyről a harmadik lejtőn alázuhan. A Lejtő aljában a <br><br>

<b>W = m *g *h = 1 kg * 10 m/s<sup>2</sup> * 1.8 m = 18 J</b> helyzeti energia <br><br>

<b>E<sub>k</sub> = m * v<sup>2</sup> / 2</b>  mozgási energiába alakul át, amiből a kezdeti sebesség <br><br>

<b>v<sup>2</sup> = 2 * E<sub>k</sub> / m = 2 * 18 J / 1 kg = 36 m<sup>2</sup> / s<sup>2</sup> = ( 6 m/s )<sup>2</sup></b>   <br><br><br><br>

<b> v = 6 m/s</b> kezdősebességgel ( <b>E<sub>k</sub> = 18 J </b>mozgási energiával) a téglatest a márványpadlón <br><br>

<b>n = 0.1</b>  súrlódási együtthatóval <b>L</b> távolságra elcsúszik<br><br>

<b>F<sub>s</sub> = n * F<sub>ny</sub> = 0.1 * 10 N = 1 N </b> súrlódási fékezőerő ellenébe.<br><br>

<b>W = F<sub>s</sub> * L</b> összefüggés alapján <br><br>

<b>L = W / F<sub>s</sub> = 18 J / 1 N = 18 m</b> messzire csúszik a téglatest.






