<br>
Bizonyos anyagok magas reflektivitással jellemezhetők a közeli-infravörös (1-3μm) tartományban: léteznek olyan speciális adalékok, mint az <b>InfraCool</b> vagy <b>EnergyStar </b>, melyek tovább fokozzák a közeli-infravörös (1-3μm) tartományban a napfény-reflektivitást.<br><br>

Akrilfestékekbe adalékolt üreges üveg/szilícium/kerámia 10-100μm átmérőjű mikrogöböcskék (gyártóik állítása szerint) ugyancsak fokozzák a közeli-infravörös (1-3μm) tartományban a napfény-reflektivitást.
<br>
Sötétebben árnyalt bevonatok esetében az eredeti napfény-reflektivitáshoz képest jelentős napfény-reflektivitás-növekményt, közel háromszoros javulást is eredményeznek ezek az adalékanyagok (mivel kevésbbé kedvező a kiindulási pont, 5%  reflektivitás helyett 20% lesz). Világos festékek esetében a napfény-reflektivitás-növekmény nem ennyire szembeötlő,  de akár 50% javulást hoz, reflektivitását 60%-ról 85%-ra fokozza.<br>
