  A két felület közötti hősugárzásos energiaátvitel lényegében csak akkor jelentős (a környezetet is felületként értelmezzük), ha nagy a hőmérsékletkülönbség közöttük:<br>

-  Amennyiben jó sugárzási hőkontaktust (energiaátvitelt sugárzás útján) kívánunk, akkor a felületek termálisan feketék kell hogy legyenek (ettől még lehetnek fehérek a látható fényben, de "feketék" hősugárzási és hőelnyelési tartományukban, ilyen például a fehér zománcfestékkel lefestett fűtőtest, radiátor, mely nevében viseli a "sugárzó" jellemzőt).<br>
  
-  Amennyiben azonban nagyon nem szeretnénk radiatív hőkontaktust (energiaátvitelt sugárzás útján), például a légkörünkbe alábukó űrszonda esetében, akkor a felületek termálisan tükrözők kell hogy legyenek (egyikük legalábbis). Ha csak az egyik felület tükröző (99% tükrözés), akkor az energiaátvitel százszor lesz kisebb, ha pedig mindkettő, akkor tízezerszer lesz kisebb az energiaátvitel. <br><br>
  
 Ha már említettük az űrszondát, akkor foglalkozzunk vele részletesebben:<br>
 - visszatérésekor a Föld légkörébe az űrszonda sebessége közel tíz kilóméter másodpercenként <br>
  - a Föld felé zuhanva sebessége csak nagyobb lesz a gravitációs gyorsulás következtében<br>
 -  idővel egy légpárnát kezd maga előtt tolni mely olyan hevesen súrlódik a többi még nyugvó levegőrészecskével, hogy az egész mint egy lángcsóva "meggyullad"<br>
 - mint egy hullócsillag az éjszakai égbolton eléghet az űrszonda is...<br>
 - valójában nem is a meteor súrlódik a levegővel amitől izzásba jön és elég, hanem az előtte lángoló levegőpárna izzó tüzének kisugárzása porlasztja semmivé, megolvasztja és azonnyomban elgázosítja a meteor felszíni rétegét, egyiket a másik után, milliméterről milliméterre lehámozva a kőzetanyagot...<br>
 - a nemfémes anyagok igen hatékonyan nyelik el a sugárzást, kevésbbé jól vezetik a hőt a fémes anyagoktól, így a felszín lepárolgása igenis valószínűsíthető egy légkörrel rendelkező bolygóra való alázuhanáskor<br>
 - sem hővezetéssel sem hőáramlással nem lehet olyan nagy mennyiségű energiát átadni mint hősugárzással, a lángcsóva melyet a meteor maga előtt tol olyan nagy mennyiségű energiát sugároz mindenfelé, de leginkább magára a meteorra, hogy annak a felszíne ugyanúgy el fog párologni még akkor is, ha fémből van, amiből kifolyólag százszor kevésbbé nyeli el a hőt és százszor gyorsabban elvezeti azt a mélyebb rétegekbe...mert olyan mértékben zúdul rá a sugárzás ...<br>
 - a magas hőmérsékletre felhevült fém felülete igen rossz hatásfokkal képes leadni, kisugározni a felvett hőenergiát <br>
 - fém borítása az űrszondának nem lehet, mert annak ellenére, hogy hatékonyan tükrözné a hőt, mégis a végén olyan nagy mennyiségű hőenergiát szedne fel, hogy felszíne megolvadna (és elkezdene párologni), az utastérben pedig pecsenyesütöde effektus lépne fel...<br>
 - a kerámiaanyagok kristályszerkezetükből kifolyólag jobban állják a magas hőmérsékletet mint a fémek, nehezebben olvadnak meg, ugyanakkor kevésbbé jól vezetik a hőt a mélyebb rétegekbe...<br>
- a magas hőmérsékletre felhevült kerámia felülete amellett, hogy igen hatékonyan tükrözi a napsugárzás (és az előtte taroló tűzcsóva) látható fényét és közeli infrasugárzását (1μm körüli hullámhosszak, 1eV nagyságú fotonenergiák, 3000C hőmérséklet), igen magas hatásfokkal képes leadni, kisugározni a felvett hőenergiát  a közepes infratartományban (4μm körüli hullámhosszak, 1/4eV nagyságú fotonenergiák, 500C körüli felszíni hőmérséklet)<br>
- egy kis számítás: 1450C hőmérsékletű feketetest-sugárzása 500kW/m<sup>2</sup> (ötszázszoros napsugárzás!), erre az 1450C  hőmérsékletre felhevített levegő sugárzása (tekintettel arra, hogy pont az 1-3μm tartományban van az első kozmikus ablak, a látható fény, a tűz ablaka) megközelítőleg 50kW/m<sup>2</sup> (még mindig ötvenszeres napsugárzás!), ebből a fém is meg a kerámia is 90%-ot tükröz, csak 10%-ot nyel el, ami 5kW/m<sup>2</sup>. 500C hőmérsékletű feketetest-sugárzása 20kW/m<sup>2</sup>, azonban a fém nehezen sugároz, 5% hatékonysággal csak 1kW/m<sup>2</sup> hőenergiát képes kisugározni. A fémfelület 900C fokra fog felmelegedni ahhoz, hogy 5kW/m<sup>2</sup> hősugárzást tudjon kibocsájtani 5%-os hatékonysággal. Az 500C forró kerámia 90%-os hatékonysággal 18kW/m<sup>2</sup> hőenergiát képes kisugározni, így 300C alá sűlyed hőmérséklete amelyen pont 5kW/m<sup>2</sup> hősugárzást fog kibocsájtani 90%-os hatékonysággal. <br> <br> 

