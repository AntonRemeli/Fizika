így a  lineáris tömeg (<b> m </b>), lineáris lendület (<b> I </b>) és lineáris erőhatáson (<b> F </b>) túl a "körtömeg", "körlendület" és "körerő" fogalmát is be kell vezetni ahhoz, hogy a búgócsiga őrült pörgésével elmesélhesse a világ történetét:
<i>
<br><br>
(<b> n </b>)<b> KÖRTÖMEG</b>: A nyugvó rendszer (mely állhat akár csak egy objektumból vagy objektumok tömkelegéből) megtartja nyugalmi állapotát mindaddig, amíg egy külső hatás annak megváltoztatására nem kényszeríti... ezen tehetetlenség mértéke a <b> körtömeg </b>(<b> n = m * k </b>, idegen szóval: <b> "moment of inertia" = "tehetetlenségi nyomaték"</b>,  vagy <b>"rotational inertia" = "forgási tehetetlenség"</b>, avagy <b>"angular mass" = "szögtömeg"</b>), mely megmutatja, mennyire nehéz valamit "kifordítani" helyzetéből (<b> k </b> a körpálya középpontjától, a forgási tengelytől mért távolság, az erőkar hossza).
<br><br>
(<b> N </b>)<b> KÖRLENDÜLET</b>: A <b>körmozgásmennnyiség</b> ( <b>N = I * k </b>, jó magyar elnevezései <b>"perdület", "impulzusnyomaték"</b> avagy <b>"lendületnyomaték"</b>, idegen szóval: <b>"angular momentum" = "szöglendület"</b> és <b>"rotational momentum" = "forgási lendület"</b>) megmutatja, hogy egy adott dolog mennyire akarja megtartani a körforgását, vagy kölcsönhatáskor mekkora körlendületet adhat át egy másik (helyhezkötött de körmozgásra képes) testnek
<br><br>
(<b> M </b>)<b> KÖRERŐHATÁS</b>: Az erő forgatónyomatéka okozza a kölcsönható testek körlendületváltozását: <b> M = ΔN/Δt </b>("Δ" a változás jele, továbbfejtve, <b>N = I * k </b> behelyettesítésével az erőkar törvényét kapjuk:) <b>= ΔI/Δt * k = F * k</b> ( idegen szóval: <b>"torque" = "forgatónyomaték", "momentum" = "nyomaték"</b>, vagy <b>"moment of force" = "erőnyomaték"</b> )
