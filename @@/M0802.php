Érdekességként megvitatandó még a 207C forró (3000W/m<sup>2</sup> hősugárzás) szigetelés nélküli tükröző aluminiumfalú sütő. A külső 17C meleg környezet 400W/m<sup>2</sup> hősugárzásából  csak 1%, azaz 4W/m<sup>2</sup> hősugárzást fog elnyelni, 207C forró külső fala pedig 30W/m<sup>2</sup> hősugárzást  fog kisugározni (1% a 3000W/m<sup>2</sup>-ből), tehát a kiáramló hő mennyisége 26W/m<sup>2</sup> (=30-4). A 760W/m<sup>2</sup> (=190x4=200x4-40) beltéri hőátadást rövid időre felejtsük el (hagyományos szigetelés szükséges ilyen magas hőátadás kiküszöbölésére). <br><br>
Amennyiben a külső oldalát fehérre festjük, úgy a környezeti hősugárzás 90%-át el fogja nyelni, ami 360 W/m<sup>2</sup> (=400-10%) . Mivel külső fala most is 207C forró, úgy a 3000W/m<sup>2</sup> 90%-át fogja kisugározni, ami 2700W/m<sup>2</sup> . Tehát jelentős 2340W/m<sup>2</sup> lesz a kiáramló hő mennyisége  a 26W/m<sup>2</sup> helyett, ami kilecvenszer több. A sütőt ebből kifolyólag nem szabad fehérre festeni (de ha már befestettük, akkor lehet akár fekete is)!
<br><br>
A téves bevonat választásának következménye kellő vastagságú szigeteléssel eltusolható, de nem küszöbölhető ki:<br>

10cm kőzetgyapot belső hőszigeteléssel (0.4W/m<sup>2</sup>K hővezetés), a sütő külső felülete 207C helyett 27C lesz (180x0.4=72W a hővezetés), csak tíz fokkal melegebb a környezettől: 27C sugárzása 460W/m<sup>2</sup>, tükröző aluminium esetében 4,6W/m<sup>2</sup>, a környezeti 400W/m<sup>2</sup> sugárzásból 4W/m<sup>2</sup> képes elnyelni, így nettó energiafelvétele 4.6-4=0.6W/m<sup>2</sup>. Fehér felület energiafelvétele hasonló megfontolásokkal 414-360=54W/m<sup>2</sup> lesz. De így is a tükröző felület kilecvenszer kevesebb hőt sugároz ki.<br><br>
 
 A hőátadás figyelembevételével a sütő esetében 9C hőmérsékletkülönbség  36W/m<sup>2</sup> hőátadást eredményezne, ami lehetséges, mert 10cm kőzetgyapot belső hőszigetelés legfeljebb 72W/m<sup>2</sup> hővezetésre képes a  180C hőmérsékletkülönbségnél. A hősugárzás 0.05W/m<sup>2</sup> a tükröző aluminiumoldal esetén, és  36W/m<sup>2</sup> a fehérre festett felület esetén (kilecvenszer  több ugyan most is, de 50-szer kisebb a szigetelt falú sütő 36W/m<sup>2</sup> hőkisugárzása a szigetelés nélküli fehér felület 2340W/m<sup>2</sup> hősugárzásától).<br><br>
 
 Mivel a szigetelt, tükröző alumíniumfalú sütő felszíni hővesztesége csak 36W/m<sup>2</sup>, hővezetéssel azonban ennek kétszerese érkezik a külső felszínére, úgy a tükröző felület 35C fokra fog felmelegedni, mely hőmérsékleten a levegőáram elviszi az összes 72W/m<sup>2</sup> hőenergiát (72=18*4) .
  <br><br>
  A fent kifejtettek alapján megállapíthatjuk, hogy a 10cm kőzetgyapot belső hőszigeteléssel rendelkező sütő NEM lesz kevésbbé hatékony, ha tükröző aluminiumfalát fehérre festjük (felszíne azonban érezhetően hidegebb lesz) . <br><br>
  
  A szigetelés nélküli esetben úgy a tükröző meg a fehér felület is 207C meleg, így mindkét esetben a vele érintkező hideg levegő áramlása 760W/m<sup>2</sup> energiát visz el (760=190*4=200*4-40), azonban, amíg a tükröző aluminium nettó kisugárzása 26W/m<sup>2</sup>, a fehér bevonat nettó kisugárzása 2340W/m<sup>2</sup> lesz:<br>
  - amíg a tükröző aluminiumfalú sütő hővesztesége 800W/m<sup>2</sup> körüli értéket vesz fel, addig<br>
  - a fehérre festett sütő négyszer több, 3100W/m<sup>2</sup> hőenergiát fog veszíteni!
