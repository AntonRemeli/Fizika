<p><br />A Földről az előttünk elhúzó űrhajót nézzük . Az űrhajós a mozgásra merőleges "<i><b>a</b></i>" oldal mentén a padlótól a menyezetig egy lézersugarat bocsájt ki. Az űrhajós nyugalmi rendszerében ez a fénysugár  <b>t<sub>0 </sub></b> idő alatt <b><i>a</i> = c t<sub>0</sub></b> utat tesz meg, ahol "<b>c</b>" a fény terjedési sebessége,   "<b>t<sub>0</sub></b>" az űrhajóban mért idő.</p>
<p>Számunkra, a Földröl nézve,  a menyezet  <b><i>b</i> = v t</b> szakasszal "kioldalaz" a fénysugár elől mire az eléri a menyezetet ("<b>v</b>" az űrhajó sebessége), így a fénysugár akarva-akaratlanul az átlós  <b><i>c</i> = c t</b> utat teszi meg (az első "<i><b>c</b></i>" az "<i><b>a-b-c</b></i>" háromszög átfogója, a második "<b>c</b>" a fény terjedési sebessége, "<b>t</b>" pedig a Földön mért idő amíg a fénysugár az űrhajó padlójától  a menyezetig fölér.</p>
<p>Pythagoras tétele alapján:</p>

<p><i><b>a<sup>2</sup> + b<sup>2</sup>= c<sup>2</sup></b></i> ,  azaz átcsoportosítva egyik oldalra az űrhajós, másikra a földi méréseket az: </p>
<p>
<i><b>a<sup>2</sup> = c<sup>2</sup> - b</b></i><sup><i><b>2</b></i>  </sup> összefüggést kapjuk, melybe behelyettesítve a bal oldalon az űrhajóban eltelt idő, jobb oldalon a Földön mért idő és fénysebesség és űrhajósebesség szorzatával kifejezett távolságokat:</p>
<p>
<b>c<sup>2</sup> t<sub>0</sub><sup>2</sup> = c<sup>2</sup> t<sup>2</sup> - v<sup>2</sup> t<sup>2</sup>  </b> kifejezést kapjuk, melyet "<b>c</b>"  fénysebesség négyzetével osztunk: <br />
 <b>t<sub>0</sub><sup>2</sup> = t<sup>2</sup> (1 - v<sup>2</sup> / c<sup>2</sup>)</b>  majd gyökvonással megkapjuk az űrhajóban eltelt "<b>t<sub>0</sub></b>"  idő és földi "<b>t</b>" idő kapcsolatát:<br /> 
 <b>t<sub>0</sub> = t (1 - v<sup>2</sup> / c<sup>2</sup>)<sup>1/2</sup>  </b>és <br /> 
 
 <b>t= t<sub>0</sub> (1 - v<sup>2</sup> / c<sup>2</sup>)<sup>-1/2</sup></b> .</p>


