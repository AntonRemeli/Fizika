A hőtan I. főtétele szerint:
<br>
A gáz <b> E<sub>b</sub>  </b> belső energiájának megváltozása egyenlő a gáz által felvett vagy leadott <b>  Q </b> hőmennyiség és <b>  W </b> munkavégzés összegével:

<b> &#916;E<sub>b</sub> = Q + W </b> . 
<br><br>
A továbbiakban <b> Q </b> hő közlésével <b> W = Q </b> munkát veszünk ki a rendszerből.
<br><br>

<b> A) változat, izoterm-izobár-izochor körfolyamat:</b> 
<br><br>
Fűtsünk jól alá, képzeljünk el egy <b> T<sub>0</sub> </b> hőmérsékletű és <b> p<sub>0</sub> </b> nyomású gázzal telt hengert  és benne egy dugattyút, mely <b> Q </b>  hő felvételével folyamatosan kifelé nyomódik úgy, hogy a felvett hő rögtön <b> W = Q </b> munkává alakul át és így a gáz <b> E<sub>b</sub> </b> belső energiája, azaz <b> T<sub>0</sub> </b> </b> hőmérséklete változatlan marad  (<b>izoterm </b> állapotváltozás), aminek következtében a <b> p * V = n*R*T </b> állapotegyenlet alapján  a 
<b>  p<sub>0</sub>*V<sub>0</sub> =  p<sub>1</sub>*V<sub>1</sub> </b> 
egyenlőségből a kitágult gáz nyomása 
<b>  p<sub>1</sub> = p<sub>0</sub>*V<sub>0</sub> / V<sub>1</sub> </b> 
értékre csökken.
<br><br><br>

Ahhoz azonban, hogy a dugattyú elinduljon, a külső nyomás kisebb kell hogy legyen mint a hengerben mért nyomás, mely külső nyomás általában a légköri nyomás, egy atmoszféra (100kPa), de kint az űrben a külső nyomás lehet akár nulla is. Vákuumban kapjuk a legtöbb hasznos munkát, mert ott a környező levegő széttolására nem kell munkát befektetni, de maga a hengerben táguló gáz ugyanakkora munkát ad le úgy a légköri nyomáson mint a vákuumban. Amennyiben azonban a dugattyút vissza kell nyomni kiinduló helyzetébe, akkor amennyi munka kellett a légkör eltolásához, ugyanannyi munkát a légkör vissza is ad a dugattyú visszanyomása alkalmából.
<br><br>
 A  <b> V<sub>1</sub> > V<sub>0</sub>  </b> térfogatra táguló gáz által végzett munka:
 </b><br><br>
<b> W = F * s = p<sub>á</sub> * A * s = p<sub>á</sub> * &#916;V = megközelítőleg = p<sub>0</sub> * (V<sub>1</sub>+V<sub>0</sub>) * (V<sub>1</sub>-V<sub>0</sub>) / 2 V<sub>1</sub> = p<sub>0</sub> * (V<sub>1</sub><sup>2</sup>-V<sub>0</sub><sup>2</sup>) / 2*V<sub>1</sub> </b><br>
<br>
ahol  <b> p<sub>á</sub> </b> az átlagos nyomás (első közelítésben: <b> p<sub>á</sub> = (p<sub>0</sub> + p<sub>1</sub>) / 2 = p<sub>0</sub> * (V<sub>1</sub>+V<sub>0</sub>) / 2*V<sub>1</sub> </b>,
<br> 
és  <b>  &#916;V = A * s =  V<sub>1</sub>-V<sub>0</sub> </b> pedig a térfogatnövekmény.
 

<br><br>
Ugyanannyi <b>  W </b> munka befektetésével és <b>  Q = W </b> hőenergia elvonásával visszaállíthatjuk az eredeti <b>  p<sub>0</sub>  </b>  nyomást és <b>  V<sub>0</sub>  </b> térfogatot egy visszafordított izoterm folyamatban az állandó <b>  T<sub>0</sub>  </b>  hőmérsékleten. Az ilymód zárt ciklussal azonban nem nyerhető mechanikai energia  hőenergiából.
<br><br>

Ezért egy izobár folyamatban tömörítsük vissza a <b>  p<sub>1</sub>  </b> nyomású gázt a kiindulási <b> V<sub>0</sub>  </b> térfogatra <b>  Q<sub>1</sub>  </b> hőenergia elvonásával (hőerőgép lehűtése <b>  T<sub>1</sub>  </b> hőmérsékletre) és <b>  W<sub>1</sub> = p<sub>1</sub> * &#916;V  </b> munka befektetésével, mely munka kisebb az izoterm tágulásban nyert <b> W =  p<sub>á</sub> * &#916;V  </b> munkától (mert <b>  p<sub>1</sub>  </b>  kisebb mint <b>  p<sub>á</sub>  </b> ).
<br><br>
Egy további izochor folyamatban az állandó kiindulási <b> V<sub>0</sub>  </b> térfogaton (nincs térfogati munkavégzés) <b> Q<sub>2</sub></b>   </b> hőenergia közlésével fűtsűk fel a hengerben lévő gázt a kiindulási <b>  T<sub>0</sub>  </b>  hőmérsékletre. Ezzel bezárult a kör: további <b>  Q  </b>   hőt közölhetünk a gázzal mely <b>  W  </b> tágulási  munkába alakul állandó <b>  T<sub>0</sub>  </b>  hőmérsékleten...
<br><br><br><br>


<b> B) változat, izochor-adiabatikus-izoterm körfolyamat:</b> 
<br><br>
Fűtsünk jól alá, képzeljünk el egy <b> T<sub>0</sub> </b> hőmérsékletű és <b> p<sub>0</sub> </b> nyomású gázzal telt hengert  és benne egy dugattyút, mely <b> Q </b>  hő felvételével először erőteljesen felmelegszik <b>  T<sub>2</sub>  </b>  hőmérsékletre anélkül hogy tágulna ( <b>  izochor </b> állapotváltozás, <b>  V<sub>2</sub>  </b> = <b>  V<sub>0</sub>  </b>), aminek következtében a <b> p * V = n*R*T </b> állapotegyenlet alapján a

<b>  p<sub>0</sub>/T<sub>0</sub> =  p<sub>2</sub>/T<sub>2</sub> </b> 
egyenlőségből a felhevített gáz nyomása 
<b>  p<sub>2</sub> = p<sub>0</sub>*T<sub>2</sub> / T<sub>0</sub> </b> 
értékre emelkedik, majd egy erőteljes rántással kinyomja a dugattyút és meghajtja a gőzgép lendkerekét.
<br><br>
 A kifelé táguló dugattyú munkát végez, egyidőben csökken a gáz nyomása és hőmérséklete  (<b>adiabatikus </b> állapotváltozás), aminek következtében a <b> p * V = n*R*T </b> állapotegyenlet alapján  a 
<b>  p<sub>2</sub>*V<sub>2</sub>/T<sub>2</sub>  =  p<sub>1</sub>*V<sub>1</sub>/T<sub>1</sub> </b> 
egyenlőségből a kitágult gáz nyomása 
<b>  p<sub>1</sub> = p<sub>2</sub>*V<sub>2</sub>*T<sub>1</sub> / V<sub>1</sub>*T<sub>2</sub> </b> 
értékre csökken. 

<br><br>
 A  <b> V<sub>1</sub> > V<sub>0</sub>  </b> térfogatra adiabatikusan táguló gáz által végzett <b>W<sub>b</sub></b> munka nagyobb lesz, mint az izochor folyamatban felvett <b>Q</b> hőenergia:
 </b><br><br>
<b> W<sub>b</sub> = F * s = p<sub>b</sub> * A * s = p<sub>b</sub> * &#916;V = megközelítőleg = p<sub>0</sub> * ( V<sub>1</sub>*T<sub>2</sub> + V<sub>0</sub>*T<sub>1</sub>   ) * (V<sub>1</sub>-V<sub>0</sub>) / 2*V<sub>1</sub>*T<sub>0</sub>  </b><br>
<br>


ahol  <b> p<sub>b</sub> </b> az átlagos nyomás (első közelítésben: <b> 
	
	p<sub>b</sub> = (p<sub>2</sub> + p<sub>1</sub>) / 2 = 
	
	p<sub>2</sub> * ( 1 + V<sub>2</sub>*T<sub>1</sub> / V<sub>1</sub>*T<sub>2</sub>  ) / 2 =

	p<sub>2</sub> * ( V<sub>1</sub>*T<sub>2</sub> + V<sub>2</sub>*T<sub>1</sub>   ) / 2*V<sub>1</sub>*T<sub>2</sub> =
	
	p<sub>0</sub>*T<sub>2</sub> / T<sub>0</sub> * ( V<sub>1</sub>*T<sub>2</sub> + V<sub>0</sub>*T<sub>1</sub>   ) / 2*V<sub>1</sub>*T<sub>2</sub> =
	
		p<sub>0</sub> * ( V<sub>1</sub>*T<sub>2</sub> + V<sub>0</sub>*T<sub>1</sub>   ) / 2*V<sub>1</sub>*T<sub>0</sub>,

</b>

<br> és  <b>  &#916;V = A * s =  V<sub>1</sub>-V<sub>0</sub> </b> pedig a térfogatnövekmény.
 
<br> <br> 
Adiabatikus folyamat esetén rövidebb úton, kisebb <b>V<sub>3</sub> < V<sub>1</sub></b> térfogatra való tágulással fogja a <b> Q </b> hőenergiával izochor folyamatban felhevített gáz leadni a <b> W = Q </b> munkát. A kérdés csupán az, hogy vajon ez a <b> T<sub>0</sub> </b> hőmérsékletre való lehüléssel párosul-e.
<br> <br> 
Egy izoterm kompresszióval a <b>V<sub>3</sub> </b> térfogatra expandált gáz a kiindulási <b> V<sub>0</sub></b> térfogatra hozható vissza <b>W<sub>3</sub></b> munka befektetésével, mely azonnyomban <b>Q<sub>3</sub> = W<sub>3</sub> </b>hőenergia alakjában el is hagyja a gázt, így a gáz belső energiája és hőmérséklete azonos a két végpontban. Tehát:
<br> <br> 
A <b> T<sub>0</sub> </b> hőmérsékletről <b> Q </b> hőenergiával izochor folyamatban felhevített gáz adiabatikus tágulás útján <b> W = Q </b> munkát ad le, és eközben T<sub>0</sub> </b> hőmérsékletre hül vissza. 













