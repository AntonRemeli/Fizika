A Föld légkörének nyomása 10m magas vízoszlop nyomásával egyenlő. Mivel egy négyzetméterben tízezer négyzetcentiméter van, a tíz köbmétereses vízoszlop súlya pedig tízezer kilogramm súlyerővel egyenlő, úgy egy négyzetcentiméterre egy kilogramm nyomóerő jut, ami:
<br><br>
<b> p  = F / A  = 10 N / cm<sup>2</sup> = 1 Atm =  1 Bar  =100 000 N/m<sup>2</sup> = 100 kPa </b> atmoszferikus nyomásnak felel meg.
<br><br><br>

Továbbá vegyük észre, hogy a <b> p </b> nyomású <b>V </b> térfogatba zárt  gáz  <b> p * V </b> szorzatának mértékegysége
<br><br>
<b> N/m<sup>2</sup> * m<sup>3</sup>  = N * m = J (! )  </b> a munka, energia mértékegységét eredményezi:  tehát a <b> p * V </b> szorzat a gáz belső energiáját tükrözi!
<br><br><br>


Habár  egy <b>s</b> élhosszú, <b>V = s<sup>3</sup></b> térfogatú kockába zárt <b>m</b> tömegű  és <b>v</b>  átlagsebességű  gáz  <u>vektoriális</u> összlendülete nulla:  <b><u>I</u> = m * <u>v</u> = 0 </b>   
(az aláhúzás vektort jelent, az egymással ellentétes irányba mozgó részecskék lendületei, sebességei semlegesítik egymást), 
<br><br>
a részecskék  egyenkénti lendületeinek <b>skaláris</b> összege  <b>I = m * v  >  0 </b>  annál nagyobb minél forróbb a <b>V = s<sup>3</sup></b> térfogatú  kockába zárt  <b>m</b> tömegű  gáz ( <b>I = m * v  </b>  a gázt képező összes kicsi részecske egyenkénti lendületének összege, ahol <b> m  </b> a részecskék össztömege, <b> v  </b> pedig az átlagos sebességük ). 
<br><br><br>


Az átellenes falakról <b> Δt = s / v</b> időközökben ide-oda pattogó,  egy-egy ütközésnél <b> Δv = 2*v</b>  sebességmegváltoztató részecskék 
<br><br>
<b>F = ΔI / Δt = m * Δv / Δt = m * 2*v / (s/v) = 2 m * v<sup>2</sup> / s </b> erőt fejtenek a kocka hat  <b> A = 6 * s<sup>2</sup></b> felületű oldalára,
<br><br>
tehát a <b>V = s<sup>3</sup></b> térfogatú kockába zárt gáz nyomása: <b>p = F / A = ( 2 m * v<sup>2</sup> / s ) /  ( 6 * s<sup>2</sup> ) = 2 m * v<sup>2</sup> / 6 s<sup>3</sup> = m * v<sup>2</sup> / 3 V</b> ,
<br><br>
azaz a  <b>V </b> térfogatú kockába zárt <b>p </b> nyomású gáz belső energiája: <b>p * V =  m * v<sup>2</sup> / 3 =  2/3 m * v<sup>2</sup> / 2  = 2/3 E<sub>k</sub></b>,
<br><br>
ahol <b>   E<sub>k</sub> = m * v<sup>2</sup> / 2  = 3/2 p*V </b>   a  <b>V </b> térfogatú kockába zárt <b>p </b> nyomású gáz részecskéinek mogási energiája.
