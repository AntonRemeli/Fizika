A dinamika alapvető törvényeit pontszerű testekre vonatkoztattuk: <br><br>

az <b>F</b> nagyságú erő az <b>m</b> tömegű pontra hatva megváltoztatja annak <b> I = m * v </b> lendületét <b>a</b> nagyságú gyorsulást adva a testnek mindaddig amig az <b>F</b> erő hat rá: <b> a = F / m </b>.
<br><br>

A <b> Δt </b>  ideig ható <b> F </b> erő <b> ΔI = F * Δt </b> löketet ad az addig <b> I = m * v </b> lendülettel rendelkező pontszerű testnek.<br><br>

A fentiek alapján az <b> F </b> erő értelmezhető úgy, mint a <b> Δt </b> idő alatt <b> ΔI </b> lendületváltozást előidéző hatás: <b> F = ΔI / Δt</b>, mely a lendület képletének behelyettesítésével Newton második törvényét eredményezi: <br><br>

<b> F = ΔI / Δt = Δ(m*v) / Δt = m * Δv / Δt = m * a </b>
<br><br>

A következőkben az <b> F </b> erő már nem pontszerű, hanem kiterjedt szilárd testekre hat, az alábbi alakváltozásokat előidézve a szilárd testekben:
<br><br><br><br>

<b>Nyomás</b>
<br><br>
Az <b> F </b> erő <b> A </b> nagyságú felülettel nyomja a szilárd test felszínét, ezzel mintsem <b> p = F / A </b> nyomást kifejtve, mely hatására a felület bizonyos mértékben behorpad, mely benyomódás annál nagyobb minél kisebb <b> A </b> felülettel hat a minél nagyobb <b> F </b> erő. 
<br><br>
A nyomás mértékegysége a Pascal: <b> 1 Pa = 1 N / m<sup>2</sup> </b> 
<br><br><br><br>


<b>Nyúlás</b>
<br><br>
Olyan merev testre, mely <b> H </b> hosszúsága nagy az <b> A </b> keresztmetszetéhez képest (nyilvánvalóan huzalról van szó), a rá ható <b> F </b> erő   <b> D = F * d </b> megnyúlást eredményez, ahol <b> d = e * H / A </b> az adott huzal elasztikus együtthatója, mely annál nagyobb, minél hoszabb és minél vékonyabb a huzal. <br><br>

A <b>  D / H = r </b> hányados az adott huzal relatív megnyúlása, az <b>  F / A = f </b> hányados az adott huzalban fellépő rugalmas feszültség, mértékegysége  Pascal: <b> 1 Pa = 1 N / m<sup>2</sup> </b> .
<br><br>

Hooke-törvénye kimondja, hogy a relatív megnyúlás az anyagban keltett rugalmas feszültséggel arányos: <b> r = f * e </b>
<br><br>
<b> e </b> az adott anyagra jellemző elasztikus együttható, melynek inverze <b> E = 1 / e </b> az úgynevezett rugalmassági Young-modulus mely mértékegysége ugyancsak  Pascal: <b> 1 Pa = 1 N / m<sup>2</sup> </b> . 
<br><br>
<br><br>


<b>Hajlítás</b>
<br><br>
Olyan merev testre, mely <b> H </b> hosszúsága nagy annak <b>  b </b>  vastagságához képest, ahol <b> a </b> a szélessége és  <b> A = a * b </b> a keresztmetszete   (nyilvánvalóan lemezről van szó), a rá ható merőleges <b> F </b> hajlítóerő <b> H / b/2 </b> arányban (H-b/2 erőkar) fokozott <b> F<sub>Hb</sub> = H / b/2 * F  </b> húzóerő alakjában fejti ki hatását a lemez középső síkjában. Tehát a hajlítás nyújtásra vezethető vissza.<br><br>

Egyszerű nyújtásnál az <b> F </b> erő egyenletesen oszlik meg az <b> A </b> keresztmetszeten, az <b> A </b> felületen megoszlott visszahúzó erők összege a húzóerővel lesz egyenlő (hatás - ellenhatás). Hajlítás esetében azonban az <b> F<sub>Hb</sub> </b> húzóerő egyenetlenül oszlik meg a lemez keresztmetszetén: nulla az alsó síkban (nulla megnyúlás), legnagyobb a felső síkban, gyakorlatilag <b> F<sub>Hb+</sub> = 2 * F<sub>Hb</sub> = 2 * H / b/2 * F = 4 * H/b * F </b>.

Ezáltal a felső síkban megduplázott <b> F<sub>Hb+</sub> = 4 * H/b * F </b> húzóerő <b> D<sub>Hb+</sub> = F<sub>Hb+</sub> * d </b> mértékben megnyújtja  a lemez felső felületét  ( valójában a megnyúlás feleakkora, ami helyett ugyanilyen mértékben összenyomódik az  alsó felület, amit a kifejtés egyszerűsítése végett mellőzhetünk), így a <b> D<sub>Hb+</sub> </b> megnyúlás <b> H / b </b> mértékben fokozott <b> D = H/b * D<sub>Hb+</sub>  </b> elhajlást eredményez.
<br><br>

Az elhajlásra a felső összefüggések felhasználásával:<br><br>

<b> D = H/b * D<sub>Hb+</sub> = H/b * F<sub>Hb+</sub> * d = H/b * 4 * H/b * F * d = 4 * H/b * H/b * F * d </b> összefüggést   kapjuk, ahol <b> d = e * H / A  </b> az adott lemez elasztikus együtthatója, mely annál nagyobb, minél hoszabb, vékonyabb és keskenyebb a lemez. <br><br>


<b> e </b> az adott anyagra jellemző elasztikus együttható, melynek inverze <b> E = 1 / e </b> az úgynevezett rugalmassági Young-modulus mely mértékegysége  Pascal: <b> 1 Pa = 1 N / m<sup>2</sup> </b>.
<br><br>
<br><br>


<b>Nyírás</b>
<br><br>
Olyan hasáb alakú merev testre, mely <b> H </b> magassága összemérhető annak <b> A = a * b </b> alapzatához képest, ahol <b> a </b> a szélesség  és <b> b </b> a hosszúság, a rá ható alapzattal párhuzamos <b> F </b> erő   <b> D = F * d </b> elvetemedést eredményez, ahol <b> d = g * H / A  </b> az adott lemez elasztikus együtthatója, mely annál nagyobb, minél magasabb,  keskenyebb és rövidebb a hasáb. 
<br><br>

Első pillantásra a nyírás azonos a hajlítással, azonban itt a <b> H </b> magasság nem sokkalta nagyobb a <b> b </b> oldaléltől, így nem alakul ki egy megnyúlás  <b> H </b> irányában, hanem a <b> H </b> oldalél megdőlése <b> b </b> irányában (azáltal, hogy a vízszintes <b> A = a * b </b> felületmetszetek elcsúsznak/elnyúlnak egymáson <b>  b </b> irányában,  a nyírás is nyújtásra vezethető vissza, azzal a különbséggel a hagyományos nyújtáshoz képest, higy itt az atomsíkok diszlokációja nem merőleges az <b> A </b> keresztmetszetre, hanem párhuzamos azzal).
<br><br>
A <b>  D / H = r </b> hányados az adott hasáb eredetileg az alapzatra merőleges oldalélek dőlésszöge, az <b>  F / A = f </b> hányados az adott hasábban fellépő nyírási feszültség, mértékegysége  Pascal: <b> 1 Pa = 1 N / m<sup>2</sup> </b> .
<br><br>

Hooke-törvénye kimondja, hogy a vetemedési szög az anyagban keltett nyírási feszültséggel arányos: <b> r = f * g </b>.
<br><br>
<b> g </b> az adott anyagra jellemző elasztikus együttható, melynek inverze <b> G = 1 / g </b> az úgynevezett nyírási/csúsztatási/torziós Young-modulus mely mértékegysége ugyancsak  Pascal: <b> 1 Pa = 1 N / m<sup>2</sup></b>, és megközelítőleg kétszerese a rugalmassági Young-modulusnak: <b> G = 2 * E </b>. 

<br><br>
Egyszerű nyújtásnál az <b> F </b> erő egyenletesen oszlik meg az <b> A </b> keresztmetszeten és merőleges az <b> A </b> felületre, az <b> A </b> felületen megoszlott visszahúzó erők összege a húzóerővel lesz egyenlő (hatás - ellenhatás). Nyírás esetében azonban az <b> F </b> nyíróerő egyenetlenül oszlik meg a hasáb vízszintes keresztmetszetein: nulla a legalsó síkban (nulla "elnyúlás"), legnagyobb a legfelső síkban, ahol gyakorlatilag <b> F<sub>+</sub> = 2 * F </b>.

<br><br>
Az <b> F<sub>+</sub> </b> erő   <b> D = F<sub>+</sub> * d </b> elvetemedése ezesetben <b> d = e * H / A  </b>  elasztikus együtthatóval jellemezhető, ahol <b> e = 2 * g </b> az adott anyagra jellemző elasztikus együttható, melynek inverze <b> E = 1 / e = 1 / 2*g = G / 2 </b> az úgynevezett rugalmassági Young-modulus mely mértékegysége  Pascal: <b> 1 Pa = 1 N / m<sup>2</sup> </b>.
<br><br><br><br>


<b>Csavarás</b><br><br>

Olyan kör keresztmetszetű rúd vagy huzal, mely <b> H </b> hosszúsága általában nagyobb a kör <b> R </b> sugarától, ahol <b> A = Pi * R<sup>2</sup> </b> a rúd vagy huzal keresztmetszetének nagysága,  kerületére ható tangenciális <b> F </b> erő   <b> D = F * d </b> csavarodást eredményez, ahol <b> d = g * H / A  </b> az adott rúd vagy huzal elasztikus együtthatója, mely annál nagyobb, minél hoszabb és vékonyabb a rúd vagy huzal. <br><br>

A <b>  D / H = r </b> hányados az adott rúd vagy huzal hossztengelyével eredetileg párhuzamos oldalélek dőlésszöge, az <b>  F / A = f </b> hányados az adott rúd vagy huzalban fellépő nyírási feszültség, mértékegysége  Pascal: <b> 1 Pa = 1 N / m<sup>2</sup> </b> .
<br><br>

Hooke-törvénye kimondja, hogy a vetemedési szög az anyagban keltett nyírási feszültséggel arányos: <b> r = f * g</b>, ugyanez kifejezhető a <b>  D / R = c </b> csavarodás szögével is: <b> c = f * g * H / R </b>.
<br><br>
<b> g </b> az adott anyagra jellemző elasztikus együttható, melynek inverze <b> G = 1 / g </b> az úgynevezett nyírási/csúsztatási/torziós Young-modulus mely mértékegysége ugyancsak  Pascal: <b> 1 Pa = 1 N / m<sup>2</sup></b>, és megközelítőleg kétszerese a rugalmassági Young-modulusnak: <b> G = 2 * E </b>. 

<br><br>

A csavarás is, körkörös nyírás lévén, a legvégén a nyújtásra vezethető vissza, azzal a különbséggel a hagyományos nyújtáshoz képest, higy itt az atomsíkok diszlokációja nem merőleges az <b> A </b> keresztmetszetre, hanem párhuzamos azzal.

