Nem feltétlenül! És különösen akkor nem, ha bizonyos tényezőket kifelejtünk. 
Ezért megpróbálunk kellő körültekintéssel gondolkozni. És még akkor sem 
lehetünk biztosak abban, hogy mindent figyelembe vettünk. 
Perdöntő bíró csak egy van: a KÍSÉRLET.
<br><br>
Azonban, ha csak kísérletekre építenénk boldogulásunkat a világban,
akkor végtelenül sok kísérletre volna szükségünk. Tehát marad mégis az elmélkedés:
<br><br>
Amennyiben olyan teremben vagyunk, melyben némely testek magasabb T+dT
hőmérsékletűek a környező T hőmérsékletű felületekhez képest, akkor
alapfelállásban, normális bevonatok esetében a hőtükrözés (és annak
reciproka a hőelnyelés=hősugárzás=1-hőtükrözés, rövidebben: a=e=1-r)
azonos a környező falak
T=290K (17C fok) és a hősugárzó felület T+dT=310K (37C fok)
hőmérsékletén.<br>
<br>
Azok a speciális festékek, melyek elnyelőképessége nagyobb a T+dT
hőmérsékleten mint sugárzóképességük T hőmérsékleten, kénytelenek
lesznek T hőmérsékletüket némileg megemelni&nbsp; T+ddT -re ahhoz, hogy
leadhassák az általuk elnyelt energiát (amennyiben az áramlásos és
vezetéses hőveszteségek gyakorlatilag azonosak maradnak a T és T+ddT
hőmérsékletű
felületek mentén), az így megnövekedett hősugárzás T+ddT hőmérséklet
negyedik hatványával lesz arányos, ami kicsi ddT esetében
T<sup>4</sup>*(1+4*ddT/T)
lesz ( ugyanis ddT=3C csak 1% a T=290K-hez viszonyítva).<br>
