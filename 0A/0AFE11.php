penészgomba. A megemelt hőmérsékletű falfelület kevésbbé hajlamos
páralecsapódásra, és ebből kifolyólag kevésbbé hajlamos penészesedésre. 
Az imént elhangzott gondolat egy állítás. Felvetődik a kérdés: 
engedelmeskedik-e a valóság a gondolataink által szabott szabályoknak?
