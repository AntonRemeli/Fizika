  <br><br>
  Az ENERGIA-IMPULZUSMEGMARADÁS törvénye az az alapelv, mely magába foglalja Newton három törvényét:
  <br>
  - az INERCIA (tehetetlenség) törvénye az impulzusmegmaradás következménye, tömeg a mértéke,
  <br>
  - az ERŐHATÁS az impulzus/mozgásmennyiség egységnyi időben történő megváltozásának mértéke<bold> F = dI/dt </bold>("d" a változás jele),
  <br>
  - a HATÁS-ELLENHATÁS a két kölcsönható test együttes impulzusának megmaradásán alapul<bold> dI<subold>1</subold> + dI<subold>2</subold> = 0</bold>, tehát  <bold>dI<subold>1</subold> = -dI<subold>2</subold></bold> .
  <br><br>
 A következő állításokat fogalmazhatjuk meg továbbá az impulzus vonatkoztatásában:
<br>
-  ugyanaz az erőhatás ugyanakkora impulzusváltozást idéz elő bármely testen, függetlenül annak tömegétől (<bold> F*dt = <big><big><big>m</big></big></big> * <small>dv</small> = <small>m</small> * <big><big><big>dv</big></big></big></bold>)
<br>
- zárt rendszer összimpulzusa változatlan marad az idő folyamán <bold> I<subold>1</subold> + I<subold>2</subold> + I<subold>3</subold> + I<subold>4</subold> + ...  + I<subold>n</subold> = const </bold>
  
