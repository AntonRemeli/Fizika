Eddig pontszerű testek mozgatásával foglalkoztunk. A test nyugalomban marad, ha nem hat rá erő, vagy két ellentétesen ható erő egyensúlyban van:
<b> F<sub>1</sub> = - F<sub>2</sub> </b>, azaz az eredő erő nulla: <b> F<sub>1</sub> + F<sub>2</sub> = 0 </b>.
<br><br>
Ha véges kiterjedésű testet az erőhatás oldalról kapja el, akkor a test megpördül. 
<br>
Egyik pontjában helyhez kötött test (például: hinta) erő hatására elfordul a forgástengelye mentén. Az erő <b> forgatónyomatékot (momentum) </b> fejt ki a testre, a forgatónyomaték nagysága:  <b> M = F * k </b>, ahol <b>  k </b> a forgástengelytől mért távolság, az erőkar nagysága.
<br><br>
A véges kiterjedésű test nyugalomban marad, ha nem hat rá erő, vagy az erőkarra ható erők egyensúlyban vannnak, azaz forgatonyomatékaik kölcsönösen semlegesítik egymást:
<b> M<sub>1</sub> = - M<sub>2</sub> </b>, kifejtve: <b> F<sub>1</sub>*k<sub>1</sub> = - F<sub>2</sub>*k<sub>2</sub> </b> azaz az eredő forgatonyomaték nulla: <b> M<sub>1</sub> + M<sub>2</sub> = 0 </b> , kifejtve:  <b> F<sub>1</sub>*k<sub>1</sub> + F<sub>2</sub>*k<sub>2</sub> = 0 </b>.

<br><br>
Szabad pontszerű test mozgására az <b> F </b> erő hatását írhatjuk:
<br><br>

<b> F = ΔI / Δt , . . .  F = m * Δv/Δt = m * a  </b>,  . . . körpályára kényszerített test esetében pedig az <b> M </b> forgatónyomaték hatására írhatjuk:	

<br><br>

<b> M = ΔN / Δt , . . .  M = Δ(I*k)/Δt = F * k  . . . = m * a * k = m*k<sup>2</sup> * a/k = Θ * β , </b>  <br><br>
ahol <b>Θ =  m * k<sup>2</sup></b> a polárkoordinátákban definiált tehetetlenségi nyomaték ("polárkoordinátás tömegnyomaték") , <b> N = I*k  </b> a test perdülete ("körimpulzus", "impulzusnyomaték" avagy "lendületnyomaték"), <b> β = a/k  </b> pedig a szöggyorsulás.


