Napsugárzást visszaverő bevonat alkalmazásával (pl. fehér mészfesték):<br>
- r<sub>s</sub>=80%=100%-a<sub>s</sub> magas napfényreflektivitás és <br>
- a<sub>s</sub>=20%=e<sub>s</sub> alacsony napfényelnyelés, alacsony napfényemisszió/sugárzás (amit a 3000-6000K melegen izzó közeg sugároz),<br>
- r<sub>t</sub>=10%=100%-a<sub>t</sub> alacsony hőreflektivitás és<br>
- a<sub>t</sub>=90%=e<sub>t</sub> magas hőelnyelés, magas hősugárzás/emisszió (amit 300-600K meleg közeg sugároz),<br>
a 100m<sup>2</sup> napsütötte fal 2kW helyett első közelítésben  csak 400W meleget juttat a lakásba (mert  1000W/m<sup>2</sup>-ből 800-at "ledob" magáról). 
<br><br>
Második közelítésben azzal számolunk, hogy a külső fal már nem 50C fokkal melegebb a belsőtől, hanem csak 20C fokkal:
<br>

- a hővezetés 8W/m<sup>2</sup> lesz (  1m vastag nikecell hővezetési tényezője  0.04W/m<sup>2</sup>K, tizedvastagságé tízszer nagyobb:  0.4W/m<sup>2</sup>K, amit még hússzal kell megszorozni a 20 fokos előlap-hátlap hőmérsékletkülönbség folytán).<br>

- 16W/m<sup>2</sup>K konvektív hőátadási tényezővel számolunk kültéri falak esetében: 33C kültéri léghőmérséklet esetén a 40C fokra felmelegedett külső fal 112W/m<sup>2</sup> hőenergiát ad le hőáramlás útján (16*7=112 a 7C hőmérsékletkülönbségből kifolyólag). <br>

- A 40C meleg felszín hőkisugárzása 540W/m<sup>2</sup> volna feketetest esetén, így a fehér bevonat hőkisugárzása 490W/m<sup>2</sup> lesz (a<sub>t</sub>=90%=e<sub>t</sub> magas hőelnyelés, magas hősugárzás/emisszió), a 33C környezet visszasugárzása 500W/m<sup>2</sup> lenne,  azonban a kontinentális légkör visszasugárzása 400W/m<sup>2</sup>-re becsülhető (a<sub>t</sub>=80%=e<sub>t</sub> magas hőelnyelés, magas hősugárzás/emisszió), amiből kifolyólag a fehérre festett felület 90W/m<sup>2</sup> (=490-400) hőenergiát sugárzás útján ad le.
<br>
- Mindösszesen a 40C meleg napsütötte fehér fal 210W/m<sup>2</sup> hőenergiát disszipál (8+112+90=210), ami nagyjából egyensúlyban van a becsült 200W/m<sup>2</sup> elnyelt napsütéssel.
<br><br>

a 100m<sup>2</sup> napsütötte fehér fal 2kW helyett második közelítésben   0.8kW=800W (=100m<sup>2</sup>*8W/m<sup>2</sup>) meleget juttat a lakásba, tehát a 100-150 átlagosan tíz órán át napsütötte nyári nap 0.8-1.2MWh  éves hűtést igényel (8-12kWh/m<sup>2</sup> a 100m2 napsütötte falon) .
 <br>
Továbbá, az átlagos 5C fokos hőmérsékletkülönbség kompenzálására nyáron a 300m<sup>2</sup> külső falakon 2W/m<sup>2</sup> hővezetésel számolva további 600W folyamatos hűtést igényel, ami napi  15kWh energiaigény, azaz éves szinten 1.5-2.3MWh (5-8kWh/m<sup>2</sup> a 300m<sup>2</sup> külső falakra leosztva). 
<br><br>
Így az eredeti 3-5MWh összesített hűtési igény helyett egyszerű fehér mészfesték bevonat éves 2-3MWh-ra fogja felezni a ház nyári hőfelvételét (a 300m<sup>2</sup> külfalakon változatlanaul átvezetődik az átlagosan 5C fokkal melegebb külvilág, a fehér bevonat <b>nem</b> veri vissza a környezet 500W/m<sup>2</sup> hősugárzását).<br><br>

Ez a fényvisszaverő bevonat télen azonban napi  1.2kWh-val kevesebb meleget hoz a napi egyórás napsütésből (1.2=2-0.8), ezzel 0.12-0.2MWh-val növelve az éves fűtési igényt (1.2-2kWh/m<sup>2</sup>).<br>
<br>
A fényvisszaverő bevonat téli hátránya csak 10% a nyári hűtési megtakarításhoz képest, tehát közép-europában is ésszerű a falak fehérre festése. <br><br>

A mediterrán, trópusi és sivatagi övekben a fényvisszaverő bevonat sokkal több megtakarítást hoz:
<br>
akár megfelezheti a 6-10MWh éves hűtési igényét a 300m<sup>2</sup>  felületű (ebből  100m<sup>2</sup> napsütötte), nikecell szigeteléssel  rendelkező háznak.
<br><br>
Az egyszerű fehér festék (20% körüli fényelnyelési tényező) azért jobb a hasonló napfény-reflektivitással bíró fémes bevonattól, mert sokkal hűvösebb felületet biztosít azáltal, hogy hőkisugárzása gyakorlatilag akadálytalan (90% körüli hősugárzási és hőelnyelési tényező, 10% hővisszaverő képesség), míg a fémes felületek hösugárzása erősen akadályozott (1% körüli hősugárzási és hőelnyelési tényező, 99% hővisszaverő képesség), amiből kifolyólag a napsütöte fémes felületek sokkal forróbbak lesznek.
  
 
 
