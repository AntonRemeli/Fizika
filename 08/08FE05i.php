
<i>
A Föld légköre valójában NEM ÁTLÁTSZÓ az elektromágneses hullámok számára: <br>
- a légkör megvéd minket a roncsoló hatású kozmikus eredetű gamma sugárzástól (1pm hullámhossz, az atom századrésze, óriási 1MeV energia)  és <br>
- a káros kozmukis X sugárzástól is megvéd (1nm, tíz atom nagyságú, 1KeV energia), valamint <br>
- az ultraibolya fénytől is részleges védelmet nyújt a légkörünk  (100nm, ezer atom nagyságú, 10eV). <br>
- Keskeny sávban átereszti a napsugárzás látható fényét (500nm, 2eV, 6000K hőmérsékletű napfelszín kisugárzása),  ezért hisszük a légkört átlátszónak, és <br>
- átengedi a napsugárzás szemmel már nem látható felét is, a közeli infrasugárzást (1μm,  1eV, 3000K kisugárzása), <br>
- a környezeti infrasugárzás első negyedét (3-8μm)  már nem engedi át (üvegházhatás), majd <br>
- a következő keskeny sávban (8-14μm, 300K) átengedi a környezeti hősugárzás energiájának felét (termikus infrasugárzás, 0-30C környezet kisugárzása), <br>
- a környezeti hősugárzás negyedik negyedét (14-20μm) már nem engedi át (üvegházhatás),<br>
- a távoli infrasugárzást  is visszaveri (20-100μm), <br>
- 1mm/300GHz/3K a kozmikus mikrohullámú háttérsugárzás (!!), <br>
- a mikrosugárzást sem engedi át (0.1-10cm, időjárásradar érzékeli a felhőket) , majd<br>
- átlátszó a rövid és középhosszú rádióhullámok számára (10cm-1km), <br>
- a hosszúhullámok számára immár nem átlátszó...


<br><br>
Összegezve, az alapjában nem átlátszó légkörünk meghagyta az első 0.3-3μm ablakot ahhoz, hogy a Nap sugárzása a földfelszínre érhessen, majd a második "szellőző" ablakot a 8-14μm hullámhosszsávban, hogy hőguta azonban a Földet ne érhesse, és a harmadik 10cm-1km "rádiós" ablakot, hogy okostelefonozhassunk a 4G-n, GPS-ezhessünk a Google Maps-on, hogy tévézhessünk és rádiózhassunk. Hogy miért ennyire figyelmes házigazda a Földünk, a válasz pofon egyszerű: ha nem így volna, nem volna ki hogy feltegye a kérdést.
<br><br>
Az első ablakon beérkező napenergia (kb. 1000W/m<sup>2</sup>) felmelegíti a földfelszínt, így a Föld felszínének átlagos hőmérséklete 17C körül alakul. A 17C meleg földfelszín kb. 400W/m<sup>2</sup> hősugárzást bocsájt ki az ég felé. Ha ez a hősugárzás akadálytalanul elillanhatna a világűrbe, akkor a Föld annyi energiát veszítene, hogy egy jéggé fagyott hógolyó volna (mely így még jobban tükrözné a napsugárzást, és ettől még hidegebb lenne). <br><br>

A 17C környezeti hősugárzás (másnéven termikus infrasugárzás) maximuma 10μm körül van , enrgiája 3 és 20μm között oszlik meg úgy, hogy: <br>
- a környezeti infrasugárzás első negyedét (3-8μm sávban  kb. 100W/m<sup>2</sup>) nem engedi át (üvegházhatás), majd <br>
- a levegő páratartalmától független "kozmikus ablak"-on át kb. 200W/m<sup>2</sup> akadálytalanul az űrbe távozhat (ebben a 8-14μm hullámhosszsávban nincsen légköri  háttérsugárzás, a világűr -273C jéghideg lehelete érezhető), és<br>
- a környezeti hősugárzás negyedik negyedét sem engedi át (üvegházhatás, 14-20μm  sávban is kb. 100W/m<sup>2</sup> lett "visszafordítva").<br>


 <br><br>
Ez a "kozmikus ablak" életbevágóan fontos: 
<br>
nélküle a Föld túlmelegedne, az élet kipusztulna, és valószínűleg olyan meleg lenne, hogy az óceánok is elpárolognának úgy, ahogy ez már a Vénusszal megtörtént a bolygóképződés korai szakaszában.
 <br>
Tehát, a "kozmikus ablak"  létezése szerfelett fontos ahhoz, hogy a Föld élhető bolygó maradjon.<br><br>

Egy jó tipp: azok a különleges bevonatok, melyek hősugárzása kiemelkedően magas a 8-14μm hullámhosszsávban, és nagyon alacsony a két szomszédos tartományban, kétszer hatékonyabban tudnák leadni a fölösleges hőt a világűrbe  mint azok a megszokott bevonatok, melyek a teljes hősugárzási tartományban 90-99%-os sugárzási tényezővel rendelkeznek.<br><br>

Ilyen logikával a 10%-os sugárzási tényezővel rendelkező fémes tükröző felületek is a kozmikus ablakon át adják le a hőenergiát, tízszer kisebb hatékonysággal.<br><br>

Valójában, az elért hűtési hatékonyság a "minden számít" elve alapján a kozmikus ablakon át történő minél nagyobb kisugárzás és minél kisebb hő- és napsugárzás elnyelés, hővezetés és hőátadás különbözetétől függ.
 
