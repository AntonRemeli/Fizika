Az üveg hővezetése a beton hővezetésével összemérhető, 1m vastag üveg (vagy beton) 1W/m<sup>2</sup>K hőt vezet, 1cm vastag üveg hővezetése pedig 100W/m<sup>2</sup>K. Beleszámítva a kültéri 24W/m<sup>2</sup>K és beltéri 8W/m<sup>2</sup>K hőátadásokat is 6W/m<sup>2</sup>K (=1/8+1/100+1/24=12/100+1/100+4/100=17/100=/6) hővezetést kapunk, lényegében nem az 1cm üveg (és nem az 1mm sátorvászon, vagy az indiánsátor vagy jurta 2mm bőrborítása) hanem a beltéri hőátadás határozza meg az üveg-ház hőveszteségét. <br>
Ebben az esetben a beltér felszíni hőátadása képezi a meghatározó hőgátat (a hőgát=hőellenállás a hővezetés-hőátadás reciproka: 1/8=12/100 > 1/24=4/100 > 1/100, tőbb szigetelő réteg eredő hőellenállása az egyedi rétegellenállások összegéből adódik), és ezen az alapon működik az üvegház, fóliasátor, indiánsátor, mongol jurta.<br> <br> 

25C fok hőmérséklet-különbség esetén az üveg-ház falainak hővesztesége 150W/m<sup>2</sup>K lesz, tizenötször több, mint a 10cm hungarocell szigetelés esetén, tehát 10cm hungarocell tizenötször jobb hőszigetelő a sátor-megoldástól. A sátortól csak kétszer szigetel jobban a 10cm téglafal, hatszor jobban a félméteres téglafal, és tizenkétszer szigetel jobban a félméteres téglafal amennyiben nanokerámiás bevonatot kap kül- és beloldalán(és a nanokerámia felszíni hőátadása pedig valóban a gyártó állítása szerinti 2W/m<sup>2</sup>K):  <br> <br> 

- 10cm vastag  hungarocell 0.4W/m<sup>2</sup>K hőt vezet, ezért a húsz- és hatvanszor nagyobb felszíni hőátadásokat nem is vettük figyelembe (ugyanis felszíni hőátadások figyelembevételével is gyakorlatilag ugyanazt a szigetelési értéket kapjuk:  hőellenállás =1/8+1/0.4+1/24=12/100+250/100+4/100=266/100=1/0.38=1/0.4 ) . Ebben az esetben a hungarocell szigetelőréteg a meghatározó hőgát.
<br> 
- 10cm vastag  téglafal 5W/m<sup>2</sup>K hőt vezet, a kétszer és ötször nagyobb felszíni hőátadásokat most figyelembe kell vennünk (=1/8+1/5+1/24=12/100+20/100+4/100=36/100=1/2.8 ), így az eredő hővezetés 3W/m<sup>2</sup>K lesz . Ebben az esetben a téglafal és a felszíni hőátadások közösen adják meg az épület 3W/m<sup>2</sup>K hővezetésnek megfelelő szigetelését. 
<br> 
- 50cm vastag  téglafal 1W/m<sup>2</sup>K hőt vezet, ezért a nyolc- és huszonnégyszer nagyobb felszíni hőátadásokat nem is vesszük figyelembe (ugyanis felszíni hőátadások figyelembevételével is közel ugyanazt a szigetelési értéket kapjuk: =1/8+1/1+1/24=12/100+100/100+4/100=116/100=1/0.86) . Ebben az esetben a félméteres téglafal a meghatározó hőgát, a felszíni hőátadások hozománya 15%.
<br> 
- Amernnyiben azonban az 50cm vastag  téglafal, mely 1W/m<sup>2</sup>K hőt vezet, nanokerámiás bevonatot kap mely felszíni hőátadása 2W/m<sup>2</sup>K a gyártó állítása szerint,  úgy a felszíni hőátadásokat is figyelembe kell venni ( hőszigetelés =1/2+1/1+1/2=50/100+100/100+50/100=200/100=1/0.5). Ebben az esetben a félméteres téglafal és a felszíni hőátadások közösen adják meg az épület 0.5W/m<sup>2</sup>K hővezetésnek megfelelő szigetelését (amennyiben a nanokerámia felszíni hőátadása valóban a gyártó állítása szerinti 2W/m<sup>2</sup>K, amit azonban kétségbe vonhatunk, mert a felszínt az az akrilfesték alkotja, melybe beleágyaztuk a nanokerámiagömböket, a "korlátozott hőátadást" pedig nem tanúsította 0.001W/mK hővezetési tényező megmérése). 
